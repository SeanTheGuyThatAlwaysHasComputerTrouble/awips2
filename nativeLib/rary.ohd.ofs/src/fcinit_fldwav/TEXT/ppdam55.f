C
      DATA   SNAME / 'PPDAM55 ' /

      CALL FPRBUG(SNAME, 1, 55, IBUG)


      TOL=0.000001
      NUM=NUMLAD(J)
      IF(IRES.EQ.0) GOTO 100
      IF(NODESC.EQ.0) THEN
        WRITE(IPR,2000)
        WRITE(IPR,2005) (SAR(L,K,J),L=1,8)
        WRITE(IPR,2010) (HSAR(L,K,J),L=1,8)
      ELSE
        WRITE(IPR,2015) K,J,(SAR(L,K,J),L=1,8)
        WRITE(IPR,2020) K,J,(HSAR(L,K,J),L=1,8)
      ENDIF

  100 CONTINUE
      IF(NODESC.EQ.0)THEN
        WRITE(IPR,2025) LAD(K,J),HDD(K,J),CLL(K,J),CDOD(K,J),QTD(K,J),
     .       ICHAN(K,J)
        WRITE(IPR,2030)ICG(K,J),HSPD(K,J),SPL(K,J),CSD(K,J),HGTD(K,J),
     .       CGD(K,J)
        IF(CLL(K,J).GT.0.1.OR.HDD(K,J).GT.9999.) GO TO 110
        WRITE(IPR,2032)
        WRITE(IPR,2035) (HCRESL(L,K,J),L=1,8)
        WRITE(IPR,2040) (CRESL(L,K,J),L=1,8)
 110    IF(KRA.EQ.28) THEN
CC  110   IF(KRA.EQ.10.OR.KRA.EQ.14.OR.KRA.EQ.15.OR.KRA.EQ.19.
CC     .      OR.KRA.EQ.25.OR.KRA.EQ.26.OR.KRA.EQ.27) GO TO 120
CC        IF(KRA.EQ.28) THEN
          WRITE(IPR,2045) PTAR(K,J),CHTW(K,J),GZPL(K,J)
CC        ELSE
CC          WRITE(IPR,2050)
CC          WRITE(IPR,2055) (RHI(L,K,J),L=1,8)
CC          WRITE(IPR,2060) (RQI(L,K,J),L=1,8)
        ENDIF
  120   WRITE(IPR,2065) TFH(K,J),DTHDB(K,J),HFDD(K,J),
     .       BBD(K,J),ZBCH(K,J),YBMIN(K,J),BREXP(K,J),CPIP(K,J)
      ELSE
        WRITE(IPR,2070) LAD(K,J),HDD(K,J),CLL(K,J),CDOD(K,J),QTD(K,J),
     .        ICHAN(K,J)
        WRITE(IPR,2075) ICG(K,J),HSPD(K,J),SPL(K,J),CSD(K,J),HGTD(K,J),
     .      CGD(K,J)
        IF(CLL(K,J).GT.0.1.OR.HDD(K,J).GT.9999.) GO TO 210
        WRITE(IPR,2080) K,J,(HCRESL(L,K,J),L=1,8)
        WRITE(IPR,2085) K,J,(CRESL(L,K,J),L=1,8)
  210   IF(KRA.EQ.10.OR.KRA.EQ.14.OR.KRA.EQ.15.OR.KRA.EQ.19.
     .       OR.KRA.EQ.25.OR.KRA.EQ.26.OR.KRA.EQ.27) GO TO 220
        IF(KRA.EQ.28) THEN
          WRITE(IPR,2090) PTAR(K,J),CHTW(K,J), GZPL(K,J)
cc        ELSE
cc          WRITE(IPR,2095) K,J,(RHI(L,K,J),L=1,8)
cc          WRITE(IPR,2100) K,J,(RQI(L,K,J),L=1,8)
        ENDIF
  220   WRITE(IPR,2105) TFH(K,J),DTHDB(K,J),HFDD(K,J),BBD(K,J),
     .          ZBCH(K,J),YBMIN(K,J),BREXP(K,J),CPIP(K,J)
      ENDIF
