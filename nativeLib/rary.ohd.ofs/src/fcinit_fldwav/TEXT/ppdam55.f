     .//)
 2035 FORMAT(/1X,'HCRESL=',8F8.2)
 2040 FORMAT(1X,' CRESL=',8F8.2)
 2045 FORMAT(/10X,
     .'TARGET POOL ELEVATION FOR LOCK & DAM          PTAR  ',F10.2/10X,
     .'CRITICAL TAILWATER ELEVATION FOR LOCK & DAM   CHTW  ',F10.2/10X,
