C MEMBER SWMAPE
C-----------------------------------------------------------------------
C
C                             LAST UPDATE: 08/15/94.15:46:58 BY $WC21DT
C
C @PROCESS LVL(77)
C
C DESC WRITE OR UPDATE AREA MAPE PARAMETER RECORD
C
      SUBROUTINE SWMAPE (IVMAPE,XMAPEI,DESCRP,CENTRD,ISTWT,POWER,
     *   PEMEAN,PECHNG,NPE,STAID,IPDPTR,STAWT,UNUSED,LARRAY,ARRAY,
     *   IPTR,DISP,ISTAT)
C
      REAL NEW/4HNEW /,OLD/4HOLD /
      DIMENSION ARRAY(LARRAY)
      DIMENSION XMAPEI(2),DESCRP(1),CENTRD(1)
      DIMENSION PEMEAN(12),PECHNG(12),STAID(2,1),IPDPTR(1),STAWT(1)
      DIMENSION UNUSED(1)
C
      INCLUDE 'uio'
      INCLUDE 'scommon/sudbgx'
      INCLUDE 'scommon/suoptx'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/ppinit_write/RCS/swmape.f,v $
     . $',                                                             '
     .$Id: swmape.f,v 1.1 1995/09/17 19:16:12 dws Exp $
     . $' /
C    ===================================================================
C
C
C
      IF (ISTRCE.GT.1) WRITE (IOSDBG,130)
      IF (ISTRCE.GT.1) CALL SULINE (IOSDBG,1)
C
C  SET DEBUG LEVEL
      LDEBUG=ISBUG('MAPE')
C
      IF (LDEBUG.GT.0) WRITE (IOSDBG,140) IVMAPE,UNUSED,LARRAY
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
C
      ISTAT=0
C
C  CHECK FOR SUFFICIENT SPACE IN PARAMETER ARRAY
      MINLEN=39+(4*NPE)
      IF (LDEBUG.GT.0) WRITE (IOSDBG,160) MINLEN
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
      IF (MINLEN.LE.LARRAY) GO TO 10
         WRITE (LP,170) LARRAY,MINLEN
         CALL SUERRS (LP,2,-1)
         ISTAT=1
         GO TO 120
C
C  STORE PARAMETER ARRAY VERSION NUMBER
10    ARRAY(1)=IVMAPE+.01
C
C  STORE MAPE AREA IDENTIFIER
      ARRAY(2)=XMAPEI(1)
      ARRAY(3)=XMAPEI(2)
C
      NPOS=3
C
C  STORE DESCRIPTIVE INFORMATION
      DO 20 I=1,5
      NPOS=NPOS+1
      ARRAY(NPOS)=DESCRP(I)
20    CONTINUE
C
C  STORE CENTROID OF AREA (NWSRFS/HRAP COORDINATES)
      DO 30 I=1,2
      NPOS=NPOS+1
      ARRAY(NPOS)=CENTRD(I)
30    CONTINUE
C
C   STORE TYPE OF STATION WEIGHTS
      NPOS=NPOS+1
      ARRAY(NPOS)=ISTWT +.01
C
C   STORE POWER IN 1/D**POWER
      NPOS=NPOS+1
      ARRAY(NPOS)=POWER
C
C   STORE MEAN PE FOR 16TH OF EACH MONTH (MM)
      DO 40 I=1,12
      NPOS=NPOS+1
      ARRAY(NPOS)=PEMEAN(I)
40    CONTINUE
C
C   STORE DAILY CHANGE IN MEAN PE
      DO 50 I=1,12
      NPOS=NPOS+1
      ARRAY(NPOS)=PECHNG(I)
50    CONTINUE
C
C  THE NEXT TWO POSITIONS ARE UNUSED
      DO 60 I=1,2
      NPOS=NPOS+1
      ARRAY(NPOS)=UNUSED(I)
60    CONTINUE
C
C   STORE NUMBER OF PE STATIONS USED TO COMPUTE MAPE
      NPOS=NPOS+1
      ARRAY(NPOS)=NPE +.01
C
C   STORE PE STATION IDENTIFIERS
      DO 70 J=1,NPE
      DO 70 I=1,2
      NPOS=NPOS+1
      ARRAY(NPOS)=STAID(I,J)
70    CONTINUE
C
C   STORE ARRAY LOCATION OF POINTERS FOR PE DATA FOR THIS STATION
      DO 80 I=1,NPE
      NPOS=NPOS+1
      ARRAY(NPOS)=IPDPTR(I) +.01
80    CONTINUE
C
C   STORE PE STATION WEIGHTS
      DO 90 I=1,NPE
      NPOS=NPOS+1
      ARRAY(NPOS)=STAWT(I)
90    CONTINUE
C
C  WRITE PARAMTER RECORD TO FILE
      IF (LDEBUG.GT.0) WRITE (IOSDBG,150) NPOS
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
      CALL SUDOPN (1,'PPP ',IERR)
      IPTR=0
      CALL WPPREC (XMAPEI,'MAPE',NPOS,ARRAY,IPTR,IERR)
      IF (IERR.GT.0) THEN
         CALL SWPPST (XMAPEI,'MAPE',NPOS,IPTR,IERR)
         WRITE (LP,190) IERR
         CALL SUERRS (LP,2,-1)
         ISTAT=3
         ENDIF
C
      IF (LDEBUG.GT.0) CALL SUPDMP ('MAPE','BOTH',0,NPOS,ARRAY,ARRAY)
C
      IF(ISTAT.EQ.0.AND.DISP.EQ.NEW.AND.IOPMLV.GT.1)WRITE(LP,200)XMAPEI
      IF(ISTAT.EQ.0.AND.DISP.EQ.OLD.AND.IOPMLV.GT.1)WRITE(LP,205)XMAPEI
      IF (ISTAT.EQ.0.AND.IOPMLV.GT.1) CALL SULINE (LP,2)
      IF (ISTAT.EQ.0) CALL SUDWRT (1,'PPP ',IERR)
      IF (ISTAT.GT.0.AND.DISP.EQ.NEW) WRITE (LP,210) XMAPEI
      IF (ISTAT.GT.0.AND.DISP.EQ.OLD) WRITE (LP,215) XMAPEI
      IF (ISTAT.GT.0) CALL SULINE (LP,2)
C
120   IF (ISTRCE.GT.1) WRITE (IOSDBG,220)
      IF (ISTRCE.GT.1) CALL SULINE (IOSDBG,1)
C
      RETURN
C
130   FORMAT (' *** ENTER SWMAPE')
140   FORMAT (' IVMAPE=',I2,3X,'UNUSED=',F7.2,3X,'LARRAY=',I4)
150   FORMAT (' NPOS=',I2)
160   FORMAT (' MINLEN=',I3)
170   FORMAT ('0*** ERROR - IN SWMAPE - NOT ENOUGH SPACE IN PARAMETER ',
     *   'ARRAY: NUMBER OF WORDS IN PARAMETER ARRAY=',I5,3X,
     *   'NUMBER OF WORDS NEEDED=',I5)
190   FORMAT ('0*** ERROR - IN SWMAPE - UNSUCCESSFUL CALL TO WPPREC : ',
     *   'STATUS CODE=',I3)
200   FORMAT ('0*** NOTE - MAPE PARAMETERS SUCCESSFULLY ',
     *   'WRITTEN FOR AREA ',2A4,'.')
205   FORMAT ('0*** NOTE - MAPE PARAMETERS SUCCESSFULLY ',
     *   'UPDATED FOR AREA ',2A4,'.')
210   FORMAT ('0*** NOTE - MAPE PARAMETERS NOT SUCCESSFULLY ',
     *   'WRITTEN FOR AREA ',2A4,'.')
215   FORMAT ('0*** NOTE - MAPE PARAMETERS NOT SUCCESSFULLY ',
     *   'UPDATED FOR AREA ',2A4,'.')
220   FORMAT (' *** EXIT SWMAPE')
C
      END
