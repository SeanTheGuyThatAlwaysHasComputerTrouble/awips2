C MEMBER SWMAT
C-----------------------------------------------------------------------
C
C                             LAST UPDATE: 03/09/94.07:54:49 BY $WC20SV
C
C @PROCESS LVL(77)
C
C DESC WRITE OR UPDATE AREA MAT PARAMETER RECORD
C
      SUBROUTINE SWMAT (IVMAT,XMATID,DESCRP,CENTRD,BASNID,ISTWT,POWER,
     *   NTEMP,TEMPID,IPDMMT,WTMMT,UNUSED,LARRAY,ARRAY,
     *   IPTR,DISP,NSPACE,ISTAT)
C
      REAL XNEW/4HNEW /
      REAL XOLD/4HOLD /
C
      DIMENSION ARRAY(LARRAY)
      DIMENSION XMATID(2),DESCRP(1),CENTRD(1)
      DIMENSION BASNID(2),TEMPID(2,1),IPDMMT(1),WTMMT(1)
      DIMENSION UNUSED(1)
C
      INCLUDE 'uio'
      INCLUDE 'scommon/sudbgx'
      INCLUDE 'scommon/suoptx'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/ppinit_write/RCS/swmat.f,v $
     . $',                                                             '
     .$Id: swmat.f,v 1.1 1995/09/17 19:16:16 dws Exp $
     . $' /
C    ===================================================================
C
C
C
      IF (ISTRCE.GT.0) WRITE (IOSDBG,130)
      IF (ISTRCE.GT.0) CALL SULINE (IOSDBG,1)
C
C  SET DEBUG LEVEL
      LDEBUG=ISBUG('MAT ')
C
      IF (LDEBUG.GT.0) WRITE (IOSDBG,150) IVMAT,UNUSED,LARRAY
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
C
      ISTAT=0
      NUMERR=0
C
C  CHECK FOR SUFFICIENT SPACE IN PARAMETER ARRAY
      MINLEN=17 + (4*NTEMP)
      IF (LDEBUG.GT.0) WRITE (IOSDBG,170) MINLEN
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
      IF (MINLEN.LE.LARRAY) GO TO 10
         WRITE (LP,180) LARRAY,MINLEN
         CALL SUERRS (LP,2,NUMERR)
         ISTAT=1
         GO TO 120
C
C  STORE PARAMETER ARRAY VERSION NUMBER
10    ARRAY(1)=IVMAT+.01
C
C  STORE MAT AREA IDENTIFIER
      ARRAY(2)=XMATID(1)
      ARRAY(3)=XMATID(2)
C
      NPOS=3
C
C  STORE DESCRIPTIVE INFORMATION
      DO 20 I=1,5
         NPOS=NPOS+1
         ARRAY(NPOS)=DESCRP(I)
20       CONTINUE
C
C  STORE CENTROID OF AREA (LAT/LONG IN DECIMAL DEGREES)
      DO 30 I=1,2
         NPOS=NPOS+1
         ARRAY(NPOS)=CENTRD(I)
30       CONTINUE
C
C  STORE BASIN BOUNDARY IDENTIFIER
         DO 40 I=1,2
            NPOS=NPOS+1
            ARRAY(NPOS)=BASNID(I)
40       CONTINUE
C
C   STORE TYPE OF STATION WEIGHTS
      NPOS=NPOS+1
      ARRAY(NPOS)=ISTWT +.01
C
C   STORE POWER IN 1/D**POWER
      NPOS=NPOS+1
      ARRAY(NPOS)=POWER
C
C
C  THE NEXT TWO POSITIONS ARE UNUSED
      DO 50 I=1,2
         NPOS=NPOS+1
         ARRAY(NPOS)=UNUSED(I)
50       CONTINUE
C
C   STORE NUMBER OF TEMP STATIONS USED TO COMPUTE MAT
      NPOS=NPOS+1
      ARRAY(NPOS)=NTEMP +.01
C
C   STORE TEMP STATION IDENTIFIERS
      DO 60 J=1,NTEMP
         DO 60 I=1,2
            NPOS=NPOS+1
            ARRAY(NPOS)=TEMPID(I,J)
60       CONTINUE
C
C  STORE ARRAY LOCATIONS OF POINTERS FOR MAX/MIN TEMP DATA FOR STATIONS
      DO 70 I=1,NTEMP
         NPOS=NPOS+1
         ARRAY(NPOS)=IPDMMT(I) +.01
70       CONTINUE
C
C  STORE STATION WEIGHTS
      DO 80 I=1,NTEMP
         NPOS=NPOS+1
         ARRAY(NPOS)=WTMMT(I)
80       CONTINUE
C
C  WRITE PARAMTER RECORD TO FILE
      IF (LDEBUG.GT.0) WRITE (IOSDBG,160) NPOS
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
      CALL SUDOPN (1,'PPP ',IERR)
      IPTR=0
      CALL WPPREC (XMATID,'MAT ',NPOS,ARRAY,IPTR,IERR)
      IF (IERR.EQ.0) GO TO 90
         CALL SWPPST (XMATID,'MAT ',NPOS,IPTR,IERR)
         WRITE (LP,200) IERR
         CALL SUERRS (LP,2,NUMERR)
         ISTAT=2
C
90    IF (LDEBUG.GT.0) CALL SUPDMP ('MAT ','BOTH',0,NPOS,ARRAY,ARRAY)
C
      IF (ISTAT.GT.0) GO TO 110
C
C  CHECK MESSAGE LEVEL VALUE
      IF (IOPMLV.LE.1) GO TO 100
C
      IF (NSPACE.EQ.1) WRITE (LP,140)
      IF (NSPACE.EQ.1) CALL SULINE (LP,1)
      IF (DISP.EQ.XNEW) WRITE (LP,210) XMATID
      IF (DISP.EQ.XOLD) WRITE (LP,220) XMATID
      CALL SULINE (LP,1)
100   IF (ISTAT.EQ.0) CALL SUDWRT (1,'PPP ',IERR)
      GO TO 120
C
110   IF (DISP.EQ.XNEW) WRITE (LP,230) XMATID
      IF (DISP.EQ.XOLD) WRITE (LP,240) XMATID
      CALL SULINE (LP,1)
C
120   IF (ISTRCE.GT.0) WRITE (IOSDBG,250)
      IF (ISTRCE.GT.0) CALL SULINE (IOSDBG,1)
C
      RETURN
C
C- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
C
130   FORMAT (' *** ENTER SWMAT')
140   FORMAT (' ')
150   FORMAT (' IVMAT=',I2,3X,'UNUSED=',F7.2,3X,'LARRAY=',I5)
160   FORMAT (' NPOS=',I2)
170   FORMAT (' MINLEN=',I5)
180   FORMAT ('0*** ERROR - IN SWMAT - NOT ENOUGH SPACE IN PARAMETER ',
     *   'ARRAY: NUMBER OF WORDS IN PARAMETER ARRAY=',I5,3X,
     *   'NUMBER OF WORDS NEEDED=',I5)
200   FORMAT ('0*** ERROR - IN SWMAT - UNSUCCESSFUL CALL TO WPPREC : ',
     *   'STATUS CODE=',I3)
210   FORMAT (' *** NOTE - MAT  PARAMETERS SUCCESSFULLY ',
     *   'WRITTEN FOR AREA ',2A4,'.')
220   FORMAT (' *** NOTE - MAT  PARAMETERS SUCCESSFULLY ',
     *   'UPDATED FOR AREA ',2A4,'.')
230   FORMAT ('0*** NOTE - MAT  PARAMETERS NOT SUCCESSFULLY ',
     *   'WRITTEN FOR AREA ',2A4,'.')
240   FORMAT ('0*** NOTE - MAT  PARAMETERS NOT SUCCESSFULLY ',
     *   'UPDATED FOR AREA ',2A4,'.')
250   FORMAT (' *** EXIT SWMAT')
C
      END
