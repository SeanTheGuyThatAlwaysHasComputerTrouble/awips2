C DESC -- THIS SUBROUTINE SETS INFLOW EQUAL TO OUTFLOW WHEN THE MAXIMUM
C DESC -- NO. OF EXTRAPOLATIONS HAVE BEEN EXCEEDED DUE TO NONCONVERGENCE
C
      SUBROUTINE NONC55(PO,CCO,T1,LTT1,QL,LTQL,STC,LTSTC,QTC,LTQTC,
     . QSTR,LTQSR,POLH,LTPOLH,ITWT,LTITWT,NB,NGAGE,NGS,YD,QD,VD,
     . NSTR,NST,KTYP,GZO,KTIME,NPD,JN,ISTRT,DDX,IHRCO,JHRCO,JSIZE,
     . ICO,K1,K2,K4,K10,K14,K16)
C
C           THIS SUBROUTINE WAS WRITTEN  BY:
C           JANICE LEWIS      HRL   MARCH, 1999
C
C
      COMMON/FCARY/IFILLC,NCSTOR,ICDAY(20),ICHOUR(20)
      COMMON/IONUM/IN,IPR,IPU
      INCLUDE 'common/fdbug'
      INCLUDE 'common/ofs55'
C
      DIMENSION PO(*),CCO(*),T1(*),LTT1(*),QL(*),LTQL(*),STC(*),LTSTC(*)
      DIMENSION QTC(*),LTQTC(*),QSTR(*),LTQSR(*),POLH(*),LTPOLH(*)
      DIMENSION ITWT(*),LTITWT(*),NB(K1),NGAGE(K1),NGS(K4,K1),SNAME(2)
      DIMENSION NSTR(K1),NST(K14,K1),KTYP(K14,K1),GZO(K14,K1)
      DIMENSION YD(K2,K1),QD(K2,K1),VD(K2,K1),DDX(K2,K1)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_fldwav/RCS/nonc55.f,v $
     . $',                                                             '
     .$Id: nonc55.f,v 1.3 2000/07/20 12:02:53 page Exp $
     . $' /
C    ===================================================================
C
C
C
      DATA SNAME/4HNONC,4H55  /
C
C
      CALL FPRBUG(SNAME,1,55,IBUG)
C
C           SET COMPUTED TIME SERIES VALUES EQUAL TO PREVIOUSLY
C           COMPUTED VALUES
C
      DO 100 J=1,JN
      N=NB(J)
      NG=NGAGE(J)
      IF(NG.EQ.0) GO TO 100
      DO 40 K=1,NG
      KJ=LCAT21(K,J,NGAGE)
      DO 10 I=1,N
      IF(I.EQ.NGS(K,J)) GO TO 20
   10 CONTINUE
      GO TO 40
   20 LOST=LTSTC(KJ)-1
      LOQT=LTQTC(KJ)-1
      STGE=YD(I,J)
      FLOW=QD(I,J)
      IF(KTIME.EQ.0) KTIME=1
      DO 30 L=KTIME,NPD
      STC(L+LOST)=STGE
      QTC(L+LOQT)=FLOW
   30 CONTINUE
   40 CONTINUE
  100 CONTINUE
C
C        FINISH FILLING CARRYOVER ARRAY WITH PREVIOUSLY COMPUTED VALUES
C
  150 IF(IFILLC*NCSTOR.EQ.0) GO TO 200
      IF(ICO.GT.NCSTOR) GO TO 200
      TT=JHRCO-ISTRT
      CALL SAVC55(PO,CCO,CCO,QL,LTQL,POLH,LTPOLH,ITWT,LTITWT,T1,LTT1,
     . YD,QD,NB,PO(LOKRCH),DDX,PO(LONQL),PO(LOLQ1),PO(LCLQN),PO(LONLAD),
     . PO(LOLAD),PO(LOCHCTW),K1,K2,K10,K16)
cc      WRITE(6,9998)JHRCO,IHRCO,ISTRT,TT,ICO,ICDAY(ICO),ICHOUR(ICO),JSIZE
cc 9998 FORMAT(1H0,'     JHRCO     IHRCO     ISTRT   TT       ICO     ICDA
cc     *Y    ICHOUR   K4='/1H ,3I10,F5.0,3I10,I5)
C
      CALL FCWTCO(ICDAY(ICO),ICHOUR(ICO),CCO,JSIZE)
      ICO=ICO+1
      JHRCO=(ICDAY(ICO)-1)*24+ICHOUR(ICO)
      GO TO 150
C
C
C        FINISH FILLING OUTPUT TIME SERIES ARRAYS WITH PREVIOUSLY
C        COMPUTED VALUES
C
  200 DO 250 J=1,JN
      IF(NSTR(J).EQ.0) GO TO 250
      NSR=NSTR(J)
      N=NB(J)
      KJ=LCAT21(1,J,NSTR)-1
      DO 240 K=1,NSR
      DO 210 I=1,N
      IF(I.EQ.NST(K,J)) GO TO 220
  210 CONTINUE
      GO TO 240
  220 VAL=YD(I,J)-GZO(K,J)
      IF(KTYP(K,J).EQ.2) VAL=QD(I,J)
      IF(KTYP(K,J).EQ.3) VAL=VD(I,J)
      LOQS=LTQSR(K+KJ)-1
      DO 230 L=KTIME,NPD
      QSTR(L+LOQS)=VAL
  230 CONTINUE
  240 CONTINUE
  250 CONTINUE
C
  300 KTIME=NPD
C
      WRITE(IPR,8000)
 8000 FORMAT(1H0,70H**WARNING** THE TOTAL NO. OF EXTRAPOLATIONS ALLOWED
     *HAS BEEN EXCEEDED./1H ,10X,90HALL FUTURE TIME STEP VALUES HAVE BEE
     *N SET TO THE LAST COMPUTED STAGE AND DISCHARGE VALUES.//1H ,10X,63
     *HCARRYOVER ARRAYS AS WELL AS OUTPUT TIME SERIES ARRAYS WILL ALSO /
     *1H ,10X,60HBE FILLED WITH THE LAST COMPUTED STAGE AND DISCHARGE VA
     *LUES.//1H0,15X,53HFOR FURTHER EXPLANATION OF AUTOMATIC FIXIUP PROC
     *EDURE/1H ,15X,56HPLEASE REFER TO PAGE V.3.3-DWOPER-5 OF THE USERS
     *MANUAL.//1H ,10X,19HPROGRAM TERMINATED.)
      CALL WARN
C
      IF(ITRACE.EQ.1) WRITE(IODBUG,9000) SNAME
 9000 FORMAT(1H0,2H**,1X,2A4,8H EXITED.)
      RETURN
      END
