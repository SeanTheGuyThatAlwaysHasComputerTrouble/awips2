C MEMBER FRCERR
C  (from old member FCFRCERR)
C***********************************************************************
C
      SUBROUTINE FRCERR(IER)
C
C.......................................................................
C
C         FRCERR=ERROR HANDLER FOR DEFRC(RATING CURVE INPUT ROUTINE)
C
C SUBROUTINE FRCERR IS THE ERROR HANDLING ROUTINE FOR DEFRC.
C IT WILL DO THE FOLLOWING:
C  1. INCREMENT ERROR COUNTER (IERSUM)
C  2. PRINT ERROR MESSAGE ACCORDING TO THE INPUT CONDITION CODE (IER)
C  3. CALL ERROR
C
C THE SINGLE ARGUMENT IS THE CONDITION CODE, IER, WHICH HAS
C THE FOLLOWING MEANING:
C   =1, NUMBER OF FIELDS WAS IN ERROR
C   =2, NUMBER OF CHARACTERS FOR GIVEN FIELD NOT RIGHT
C   =3, GIVEN FIELD IS IN ERROR
C
C.......................................................................
C
C SUBROUTINE ORIGINALLY BY ED VANBLARGAN - HRL - JAN, 1983
C   STORED UNDER FCDEFRC IN PANVALET
C
C.......................................................................
C
      INCLUDE 'common/ionum'
      INCLUDE 'ufreei'
      INCLUDE 'common/frcers'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_top/RCS/frcerr.f,v $
     . $',                                                             '
     .$Id: frcerr.f,v 1.1 1995/09/17 18:55:00 dws Exp $
     . $' /
C    ===================================================================
C
C
C INCREMENT ERROR COUNTER AND CHECK IER
C
      IERSUM=IERSUM+1
      GO TO (100,200,300),IER
C
      WRITE(IPR,8005) IER
      GO TO 999
C
C IER=1...NUMBER OF FIELDS IS IN ERROR
C
100   WRITE(IPR,8010) NCIS,NCD,NFIELD,MXF,MNF
      GO TO 500
C
C IER=2...NUMBER OF CHARACTERS IS IN ERROR
C
200   WRITE(IPR,8020) NFIS,NCIS,NCHAR,MXC,NFC,NCD
      GO TO 400
C
C IER=3...FIELD IS IN ERROR
C
300   WRITE(IPR,8030) NFIS,NCIS,NFC,NCD
C
C WRITE OUT THE CONTENTS OF THE CARD
C
400   WRITE(IPR,8090)
      GO TO (410,420,430,440,450,460,470,480,490),NCIS
C
      WRITE(IPR,8005) NCIS
C
410   WRITE(IPR,8100)
      GO TO 500
C
420   WRITE(IPR,8200)
      GO TO 500
C
430   WRITE(IPR,8300)
      GO TO 500
C
440   WRITE(IPR,8400)
      GO TO 500
C
450   WRITE(IPR,8500)
      GO TO 500
C
460   WRITE(IPR,8600)
      GO TO 500
C
470   WRITE(IPR,8700)
      GO TO 500
C
480   WRITE(IPR,8800)
      GO TO 500
C
490   WRITE(IPR,8900)
C
C WRITE OUT CONTENTS OF CARD
C
500   WRITE(IPR,8990) IBUF
C
999   CALL ERROR
      RETURN
C
C ..........FORMATS.....................................................
C
8005  FORMAT(1H0,10X,27H**ERROR** IMPOSSIBLE VALUE=,I6)
C
8010  FORMAT(1H0,10X,41H**ERROR** IN THE NUMBER OF FIELDS ON CARD,I3,
     * 18H OF INPUT SUMMARY.,
     * 29H ACTUAL CARD SEQUENCE NUMBER=,I4
     * / 11X,14HNO. OF FIELDS=,I4,9H MAX NO.=,I4,9H MIN NO.=,I4)
C
8020  FORMAT(1H0,10X,43H**ERROR** IN NUMBER OF CHARACTERS FOR FIELD,
     * I4,8H ON CARD,I4,18H OF INPUT SUMMARY.
     * / 11X,18HNO. OF CHARACTERS=,I4,9H MAX NO.=,I4,
     * 40H ACTUAL FIELD AND CARD SEQUENCE NUMBERS=,2I4)
C
8030  FORMAT(1H0,10X,18H**ERROR** IN FIELD,I4,8H ON CARD,I4,
     * 18H OF INPUT SUMMARY.,
     * 40H ACTUAL FIELD AND CARD SEQUENCE NUMBERS=,2I4)
C
8090  FORMAT(11X,50HEXPECTED VARIABLES FOR EACH FIELD ON THIS CARD ARE,
     * 31H (OPTIONAL ARE IN PARANTHESES):)
C
8100  FORMAT(11X,40H  'ID'    ID    'ADD/REPL'  (  LAST DAY))
C
8200  FORMAT(11X,43H  STATION    RIVER    LATITUDE    LONGITUDE)
C
8300  FORMAT(11X,46H  UNITS     NUMBER OF R.C. POINTS    NUMBER OF,
     * 40H X.S. POINTS    TOTAL AREA    LOCAL AREA
     * / 11X,29H(FORECAST PRODUCTS)  ('LOOP'))
C
8400  FORMAT(11X,35H  FLOOD STG    GAGE ZERO    MIN STG,
     * 30H  (2ND FLOOD STG)  (ALERT STG))
C
8500  FORMAT(11X,39H  RECORD STG    RECORD Q    RECORD DATE,
     * 18H    RECORD COMMENT)
C
8600  FORMAT(11X,27H1,    STAGE,DISCHARGE PAIRS)
C
8700  FORMAT(11X,31H1,    ELEVATION-TOP WIDTH PAIRS)
C
8800  FORMAT(11X,38H  AREA BELOW 1ST ELEV.    BOTTOM SLOPE,
     * 30H  (  MANNING N FOR FLOODPLAIN)
     * / 11X,52H(  TIME TO PEAK    INITIAL Q    PEAK Q    INTIAL STG,
     * 11H  PEAK STG))
C
8900  FORMAT(11X,24H(  OPTIONAL INFORMATION))
C
8990  FORMAT(11X,18HCONTENTS OF CARD= ,80A1
     * / 11X,53HCHECK INPUT SUMMARY AND ANY MESSAGES BELOW TO CORRECT,
     * 60H ERROR. ALSO MORE ERROR MESSAGES MAY APPEAR DUE TO THIS ONE.)
C
C.......................................................................
C
      END
