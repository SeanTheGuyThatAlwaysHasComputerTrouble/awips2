     6       22X,'AICO     1.1 THRU 9.9',6X,F6.1/
     7       22X,'ROCO     0.0 THRU 50.00',4X,F6.2/
     8       22X,'API      0.0 THRU 6.00',5X,F6.2/
     9       22X,'AI       1.1 THRU 9.9',6X,F6.1/
     1       22X,'WE       0.0 THRU 20.00',4X,F6.2)
