   21 FORMAT(25X,'CURRENT AI',12X,2A4,5X,A4,7X,'24 HOURS')
   22 FORMAT(20X,'INPUT CARRYOVER VALUES...'/
     1       25X,'DEFAULT VALUES WERE USED.')
   23 FORMAT(20X,'INPUT CARRYOVER VALUES...'/
     1       25X,'INITIAL 24-HOUR RAINFALL/MELT VALUE SET TO ZERO'/
     2       25X,'NEWSTM    RAINCO      AICO      ROCO       ',
     3       'API        AI        WE'/
     4       27X,I4,F10.2,F10.1,2F10.2,F10.1,F10.2)
   24 FORMAT(20X,'API/AI CONSTANTS AND RECESSION FACTORS...'/
     1       29X,'C1',8X,'C2',8X,'C3',8X,'C4',8X,'C5',8X,'C6',
     2       6X,'REG1',6X,'REG2'/
     3       21X,6F10.3,2F10.2)
   25 FORMAT(/10X,'**ERROR** ILLEGAL RUNOFF ZONE NUMBER:',I6/
     1       22X,'LIMITS ARE...  101 THRU 4092')
   26 FORMAT(/10X,'**ERROR** ILLEGAL RFC ID CODE:',2X,'''',A4,''''/
     1       22X,'CHOICE MUST BE '' MKC'', '' MSP'', OR ''USER'''/
     2       22X,'NO API/AI CONSTANTS OR RECESSION FACTORS CAN BE ',
     3       'LOCATED.')
   27 FORMAT(/10X,'**ERROR** ILLEGAL API/AI RELATIONSHIP NUMBER:',I6/
     1       22X,'LIMITS ARE...  1 THRU 35'/
     2       22X,'NO API/AI CONSTANTS OR RECESSION FACTORS CAN BE ',
     3       'LOCATED.')
   28 FORMAT(/10X,'**ERROR** ILLEGAL FUTURE WEEK NUMBER:',I6/
     1       22X,'LIMITS ARE...  1 THRU 25')
