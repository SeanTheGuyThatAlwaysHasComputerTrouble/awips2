     2       22X,'ONE OF THE FOLLOWING VALUES IS OUTSIDE LIMITS.'//
     3       22X,'ITEM',7X,'LIMITS',6X,'VALUE PUNCHED'/
     4       22X,4('-'),3X,14('-'),2X,13('-')/
     5       22X,'C1      0.0 THRU 1.5',5X,F7.3/
     6       22X,'C2     -0.5 THRU 1.0',5X,F7.3/
     7       22X,'C3      2.0 THRU 6.0',5X,F7.3/
     8       22X,'C4     -1.5 THRU -0.5',4X,F7.3/
     9       22X,'C5     -2.5 THRU -0.5',4X,F7.3/
     1       22X,'C6     -0.1 THRU 0.5',5X,F7.3/
     2       22X,'REG1   0.50 THRU 1.00',3X,F7.2/
     3       22X,'REG2   0.50 THRU 1.00',3X,F7.2)
      END
