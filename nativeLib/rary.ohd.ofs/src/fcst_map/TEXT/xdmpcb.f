C MEMBER XDMPCB
C  (from old member PPXDMPCB)
C
      SUBROUTINE XDMPCB(I1,I2,I3,I4,I5,I6,I7)
C.......................................
C     THIS SUBROUTINE PRINTS MAP FUNCTION COMMON BLOCKS FOR
C        DEBUG PURPOSES.
C.......................................
C     WRITTEN BY -- ERIC ANDERSON, HRL -- MARCH 1983
C.......................................
      INTEGER*2 MSNG24,MSNG6,MSGMDR,MSNGSR
C
C     COMMON BLOCKS
      COMMON/XOPT/IXTYPE,XNAME(2),IESTPX,ICONVC,CNVDIS,CVDISW,
     1  NOEST6,IUSESR,PPUSER(2),IMOSUM,IMOWTR,IWTEST
      COMMON/XMDR/MDRSTA,ICTMDR,MDRST6,ICMDR,NCMDR,IRMDR,NRMDR,
     1 NMDR,MDR,MDRNUV
      COMMON/XDISPL/MDRPRT,MDRCOM,IPRT24,IPLT24,IPRT6,IPRSSR,
     1 IPRSRW,MAPPRT,METRIC,LASTDY,IPRDAY,IPLTMP
      COMMON/XSIZE/NMAP,NDUPL,NSLT24,NSLOT6,LDATA6,LMDR,LPPSR,
     1 MSNG24,MSNG6,MSGMDR,MSNGSR,NRECTP,MXTEMP,SMALL
      COMMON/XTIME/KZDA,KDA,KHR,LSTMO,KMO,KID,KYR,KIH,TZCODE,
     1 ISW,IUTMP,NSSR,IDAY
      COMMON/FCTIME/IZDA,IJHRUN,LZDA,LJHRUN,LZDOBS,LJHOBS,NOW(5),
     1 LOCAL,NOUTZ,NOUTDS,NLSTZ,IDA,IHR,LDA,LHR,IZHHDA
      COMMON/PUDBUG/IOPDBG,IPTRCE,NDBUG,PDBUG(20),IPALL
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_map/RCS/xdmpcb.f,v $
     . $',                                                             '
     .$Id: xdmpcb.f,v 1.1 1995/09/17 18:59:31 dws Exp $
     . $' /
C    ===================================================================
C
C.......................................
C     CHECK TRACE LEVEL
      IF(IPTRCE.GE.3) WRITE(IOPDBG,900)
 900  FORMAT(1H0,17H** XDMPCB ENTERED)
C.......................................
C     PRINT COMMON BLOCKS
      IF(I1.EQ.0) GO TO 102
      WRITE(IOPDBG,901)IXTYPE,XNAME,IESTPX,ICONVC,CNVDIS,CVDISW,
     1 NOEST6,IUSESR,PPUSER(1),PPUSER(2),IMOSUM,IMOWTR,IWTEST
 901  FORMAT(1H0,12HCOMMON/XOPT/,I5,2X,2A4,2I5,F10.2,F10.7,
     12I5,2X,2A4,3I5)
 102  IF(I2.EQ.0) GO TO 103
      WRITE(IOPDBG,902)MDRSTA,ICTMDR,MDRST6,ICMDR,NCMDR,IRMDR,
     1 NRMDR,NMDR,MDR,MDRNUV
 902  FORMAT(1H0,12HCOMMON/XMDR/,7I5,I10,2I5)
 103  IF(I3.EQ.0) GO TO 104
      WRITE(IOPDBG,903)MDRPRT,MDRCOM,IPRT24,IPLT24,IPRT6,IPRSSR,
     1 IPRSRW,MAPPRT,METRIC,LASTDY,IPRDAY,IPLTMP
 903  FORMAT(1H0,14HCOMMON/XDISPL/,12I5)
 104  IF(I4.EQ.0) GO TO 105
      WRITE(IOPDBG,904)NMAP,NDUPL,NSLT24,NSLOT6,LDATA6,LMDR,LPPSR,
     1 MSNG24,MSNG6,MSGMDR,MSNGSR,NRECTP,MXTEMP,SMALL
 904  FORMAT(1H0,13HCOMMON/XSIZE/,4I5,3I10,6I5,F10.5)
 105  IF(I5.EQ.0) GO TO 106
      WRITE(IOPDBG,905)KZDA,KDA,KHR,LSTMO,KMO,KID,KYR,KIH,TZCODE,ISW,
     1 IUTMP,NSSR,IDAY
 905  FORMAT(1H0,13HCOMMON/XTIME/,2I10,6I5,1X,A4,4I5)
 106  IF(I6.EQ.0) GO TO 107
      WRITE(IOPDBG,906)IZDA,IJHRUN,LZDA,LJHRUN,LZDOBS,LJHOBS,NOW,
     1 LOCAL,NOUTZ,NOUTDS,NLSTZ,IDA,IHR,LDA,LHR,IZHHDA
 906  FORMAT(1H0,14HCOMMON/FCTIME/,6I10,9I5,/6X,I10,I5,I10,2I5)
 107  IF(I7.EQ.0) GO TO 99
      WRITE(IOPDBG,907)IPTRCE,NDBUG,IPALL,(PDBUG(I),I=1,NDBUG)
 907  FORMAT(1H0,14HCOMMON/PUDBUG/,3I5,/6X,20(1X,A4))
C.......................................
C     CHECK TRACE LEVEL
  99  IF(IPTRCE.GE.3) WRITE(IOPDBG,919)
 919  FORMAT(1H0,14H** EXIT XDMPCB)
      RETURN
      END
