      SUBROUTINE PRP11(P)
C
C     THIS SUBROUTINE PRINTS THE PARAMETERS FOR THE
C     LAYERED COEFFICIENT ROUTING OPERATION
C
C     THIS SUBROUTINE INITIALLY WRITTEN BY
C          DAVID REED--HRL    OCT. 1979
C
      DIMENSION P(1)
C
C     COMMON BLOCKS
C
      COMMON/FDBUG/IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
      COMMON/IONUM/IN,IPR,IPU
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp11.f,v $
     . $',                                                             '
     .$Id: prp11.f,v 1.1 1995/09/17 18:49:58 dws Exp $
     . $' /
C    ===================================================================
C
C
C     CHECK TRACE LEVEL--FOR THIS SUBROUTINE =1
C
      IF(ITRACE.GE.1)WRITE(IODBUG,900)
  900 FORMAT(1H0,16H** PRP11 ENTERED)
C
C     NO DEBUG OUTPUT FOR THIS SUBROUTINE
C
      IDTQI=P(10)
      IDTQO=P(14)
      ICVAL=P(15)
      NC=P(16)
      NM1=NC-1
      NM2=NC-2
      NQST=17+NC
      NQLST=NQST+NM2
C
C     CHECK TO SEE IF ROUTING AT A REACH OR A POINT
C
      IF(IDTQO.EQ.0) GO TO 100
C
C     PRINT HEADING FOR ROUTING THROUGH A REACH
C
      WRITE(IPR,901)(P(I),I=2,6)
  901 FORMAT(1H0,10X,37HLAYERED COEFFICIENT ROUTING FOR REACH,1X,5A4)
      WRITE(IPR,902)IDTQI,NC
  902 FORMAT(1H0,10X,31HCOMPUTATIONAL TIME INTERVAL IS ,I3,6H HOURS,
     1//,11X,17HNUMBER OF LAYERS ,I2)
      WRITE(IPR,903)
  903 FORMAT(1H0,20X,34HTIME SERIES USED BY THIS OPERATION,//,16X,
     18HCONTENTS,17X,4HI.D.,9X,4HTYPE,6X,13HTIME INTERVAL)
      WRITE(IPR,904)P(7),P(8),P(9),IDTQI,P(11),P(12),P(13),IDTQO
  904 FORMAT(1H0,15X,6HINFLOW,19X,2A4,5X,A4,7X,I2,6H HOURS,//,16X,
     17HOUTFLOW,18X,2A4,5X,A4,7X,I2,6H HOURS)
      GO TO 101
C
C     PRINT HEADING FOR ROUTING AT A POINT
C
  100 WRITE(IPR,905)(P(I),I=2,6)
  905 FORMAT(1H0,15X,31HLAYERED COEFFICIENT ROUTING FOR,1X,5A4)
      WRITE(IPR,902)IDTQI,NC
      WRITE(IPR,903)
      WRITE(IPR,906)P(7),P(8),P(9),IDTQI
  906 FORMAT(1H0,15X,14HINFLOW-OUTFLOW,11X,2A4,5X,A4,7X,I2,6H HOURS)
C
C     PRINT PARAMETERS
C
  101 WRITE(IPR,907)
  907 FORMAT(1H0,15X,11HCOEFFICIENT,12X,11HRANGE (CMS))
      IF(NC.GT.1) GO TO 102
      WRITE(IPR,908) P(17)
  908 FORMAT(1H0,15X,F10.4,10X,19HQ QREATER THAN ZERO)
      GO TO 103
  102 WRITE(IPR,909)P(17),P(NQST)
  909 FORMAT(1H0,15X,F10.4,10X,15HQ LESS THAN    ,F10.3)
      IF(NC.GT.2) GO TO 104
      WRITE(IPR,910)P(18),P(NQST)
  910 FORMAT(1H0,15X,F10.4,10X,15HQ GREATER THAN ,F10.3)
      GO TO 103
  104 DO 105 I=1,NM2
  105 WRITE(IPR,911)P(17+I),P(NQST+I-1),P(NQST+I)
  911 FORMAT(1H0,15X,F10.4,10X,F10.3,5H  TO ,F10.3)
      WRITE(IPR,910)P(NQST-1),P(NQLST)
  103 RETURN
      END
