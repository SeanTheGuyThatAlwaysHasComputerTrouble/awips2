      SUBROUTINE PRP36(PL)
C.......................................
C     THIS SUBROUTINE PRINTS PARAMETER VALUES FOR THE XINANJIANG
C        BASIN MODEL OPERATION.
C.......................................
C     SUBROUTINE INITIALLY WRITTEN BY. . .
C            QINGPING ZHU -YRCC CHINA JUNE 1988     VERSION 1
C.......................................
      DIMENSION PL(1)
      DIMENSION MOPR(14)
C
C     COMMON BLOCKS.
      COMMON/FDBUG/IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
      COMMON/IONUM/IN,IPR,IPU
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp36.f,v $
     . $',                                                             '
     .$Id: prp36.f,v 1.1 1995/09/17 18:50:15 dws Exp $
     . $' /
C    ===================================================================
C
C
C.......................................
C     CHECK TRACE LEVEL -- TRACE LEVEL FOR THIS SUBROUTINE=1.
      IF (ITRACE.GE.1) WRITE(IODBUG,900)
  900 FORMAT (1H0,16H** PRP36 ENTERED)
C     NO DEBUG OUTPUT FOR THIS SUBROUTINE.
C.......................................
C     PRINT TITLE
      WRITE (IPR,903) (PL(I),I=4,8)
  903 FORMAT(1H0,10X,37HXINANJIANG BASIN MODEL OPERATION FOR ,1X,5A4)
      IDT=PL(2)
      WRITE (IPR,920) IDT
  920 FORMAT (1H0,15X,30HCOMPUTATIONAL TIME INTERVAL IS,I3,1X,6HHOURS.)
C.......................................
C     PRINT NUMBER OF UNITS
      NUNIT=PL(3)
      WRITE(IPR,921) NUNIT
  921 FORMAT(1H0,14X,24H THE NUMBER OF UNITS IS ,1X,I2,1H.)
C     PRINT TIME SERIES USED BY THIS OPERATION.
      WRITE (IPR,905)
  905 FORMAT (1H0,20X,35HTIME SERIES USED BY THIS OPERATION.)
      WRITE (IPR,906)
  906 FORMAT (1H0,15X,8HCONTENTS,14X,4HI.D.,7X,4HTYPE,5X,
     113HTIME INTERVAL)
C     PRINT EVAPORATION T.S INFORMATION (IF IET=1.)
      JDT=24
      IET=PL(11)
      IF (IET.EQ.0) GO TO 101
      WRITE (IPR,938) PL(12),PL(13),PL(14),JDT
  938 FORMAT (1H0,10X,12HPOTENTIAL ET,13X,2A4,5X,A4,7X,I2,1X,5HHOURS)
C.......................................
C     PRINT PRECIP. AND CHANNEL INFLOW T.S .
  101 J=PL(15)
      NWORD=PL(16)
      DO 113 I=1, NUNIT
      WRITE(IPR,997) I, PL(J),PL(J+1),PL(J+2),IDT
      WRITE(IPR,998) I,PL(J+3),PL(J+4),PL(J+5),IDT
  997 FORMAT(1H0,10X,19HRAIN+MELT FOR UNIT ,I2,4X,2A4,5X,A4,7X,I2,1X,
     15HHOURS)
  998 FORMAT(1H0,13X,16HINFLOW FOR UNIT ,I2,4X,2A4,5X,A4,
     17X,I2,1X,5HHOURS)
      J=J+NWORD
  113 CONTINUE
C     PRINT OPTIONS USED.
  950 J=PL(9)
      IF (J.EQ.0) GO TO 111
      WRITE (IPR,910)
  910 FORMAT(1H0,10X,47HSUMS OF WATER BALANCE VARIABLES WILL BE PRINTED)
  111 J=PL(10)
      IF (J.EQ.0) GO TO 112
      WRITE (IPR,911)
  911 FORMAT (1H0,10X,46HDETAILED SOIL-MOISTURE OUTPUT WILL BE PRINTED.)
      IF (J.EQ.1) GO TO 112
      DO 115 I=1,14
      L=J+I
      MOPR(I)=PL(L)
      IF (MOPR(I).GT.0) GO TO 115
      L=I-1
      GO TO 116
  115 CONTINUE
      L=14
  116 L=(L/2)*2
      IF (L.EQ.0) GO TO 112
      WRITE (IPR,922) (MOPR(I),I=1,L)
  922 FORMAT (1H0,15X,10HFOR MONTHS,7(I3,1H/,I4))
C.......................................
C     PRINT PARAMETER VALUES.
  112 WRITE (IPR,912)
  912 FORMAT (1H0,/,11X,40HPARAMETER VALUES - CAPACITIES ARE IN MM.)
      WRITE (IPR,913)
  913 FORMAT (1H0,14X,2H K,4X,5H IMP  ,3X,5H WUM ,3X,5H WLM ,
     13X,5H WDM ,3X,5H  SM ,3X,5H   B ,3X,5H  EX ,3X,5H   C ,3X,5H KSS ,
     23X,5H  KG ,3X,5H  CI ,3X,5H  CG )
      IPLPM=PL(19)
      WRITE(IPR,914) (PL(IPLPM-1+I),I=1,13)
  914 FORMAT(1H0,9X,2(3X,F5.2),4(3X,F5.0),2(3X,F5.1),4(3X,F5.2),3X,F5.3)
      WRITE(IPR,918) (I,I=1,12)
  918 FORMAT (1H0,10X,20H16TH OF MONTH VALUES,12I6)
      IPLET=PL(18)
      L=IPLET-1
      IF (IET.GT.0) GO TO 121
      WRITE (IPR,934) (PL(L+I),I=1,12)
  934 FORMAT (1H0,10X,16HET-DEMAND-MM/DAY,4X,12F6.1)
      GO TO 129
  121 WRITE (IPR,919) (PL(L+I),I=1,12)
  919 FORMAT (1H0,10X,13HPE-ADJUSTMENT,8X,12F6.2)
C.......................................
  129 J=PL(20)
      IF(J.EQ.1) GO TO 130
      WRITE(IPR,935)
  935 FORMAT(1H0,10X,34HDAILY ET DISTRIBUTION IS UNIFORM.  )
      GO TO 120
  130 WRITE(IPR,936)
  936 FORMAT(1H0,10X,34HDAILY ET DISTRIBUTION IS DIURNAL . )
  120 RETURN
      END
