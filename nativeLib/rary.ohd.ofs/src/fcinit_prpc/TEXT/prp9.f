C
      SUBROUTINE PRP9(PO)
C     THIS SUBROUTINE PRINTS THE PARAMETERS FOR THE
C     MUSKINGUM ROUTING OPERATION
C
C     INITIALLY WRITTEN BY
C        DAVID B. REED - HRL   OCT. 1979
C
      DIMENSION PO(1)
C
C     COMMON BLOCKS
      COMMON/FDBUG/IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
      COMMON/IONUM/IN,IPR,IPU
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp9.f,v $
     . $',                                                             '
     .$Id: prp9.f,v 1.1 1995/09/17 18:50:27 dws Exp $
     . $' /
C    ===================================================================
C
C
C     CHECK TRACE LEVEL
C
      IF(ITRACE.GE.1) WRITE(IODBUG,900)
  900 FORMAT(1H0,15H** PRP9 ENTERED)
C
C     NO DEBUG OUTPUT FOR THIS ROUTINE
C
C     PRINT TITLE
      IDTQO=PO(14)
      IDT=PO(10)
      IF(IDTQO.EQ.0) GO TO 101
C
C
C
      WRITE(IPR,901) (PO(I),I=2,6)
  901 FORMAT(1H0,15X,38HMUSKINGUM ROUTING OPERATION FOR REACH ,5A4)
      WRITE(IPR,903)IDT
      WRITE(IPR,905)
  905 FORMAT(1H0,20X,34HTIME SERIES USED BY THIS OPERATION,//,16X,8HCONT
     1ENTS,17X,4HI.D.,9X,4HTYPE,6X ,13HTIME INTERVAL)
      WRITE(IPR,902)PO(7),PO(8),PO(9),IDT,PO(11),PO(12),PO(13),IDTQO
  902 FORMAT(1H0,15X, 6HINFLOW,19X,2A4,5X,A4,7X,I2,6H HOURS,//,16X,7HOUT
     1FLOW,18X,2A4,5X,A4,7X,I2,6H HOURS)
  903 FORMAT(1H0,25X,31HCOMPUTATIONAL TIME INTERVAL IS ,I2,6H HOURS)
      GO TO 102
  101 WRITE(IPR,904)(PO(I),I=2,6)
  904 FORMAT(1H0,15X,32HMUSKINGUM ROUTING OPERATION FOR ,5A4)
      WRITE(IPR,903)IDT
      WRITE(IPR,905)
      WRITE(IPR,920)PO(7),PO(8),PO(9),IDT
  920 FORMAT(1H0,15X,14HINFLOW-OUTFLOW,11X,2A4,5X,A4,7X,I2,5HHOURS)
C
C     PRINT PARAMETER VALUES
C
  102 WRITE(IPR,906)
  906 FORMAT(1H0,10X,16HPARAMETER VALUES)
      WRITE(IPR,907)PO(15),PO(16)
  907 FORMAT(1H0,15X,24HROUTING COEFFICIENTS K =,F6.2,30H HOURS   ROUTIN
     1G PARAMETER X =,F6.2)
C
C     PRINT COEFFICIENTS C0,C1,C2
C
      WRITE(IPR,908) PO(17),PO(18),PO(19)
  908 FORMAT(1H0,10X,29HMUSKINGUM COEFFICIENTS   C0 =,F10.3,5X,4HC1 =,F1
     10.3,5X,4HC2 =,F10.3)
      RETURN
      END
