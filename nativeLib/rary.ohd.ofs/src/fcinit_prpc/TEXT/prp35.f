C MEMBER PRP35
C  (from old member FCPRP35)
C  VERSION 1
C
      SUBROUTINE PRP35(PO)
C
C
C#######################################################################
C
C  THIS SUBROUTINE PRINTS OUT PARAMETRIC DATA FOR THE API - HAR
C  OPERATION STORED IN THE PO ARRAY BY THE PIN35 SUBROUTINE.
C
C#######################################################################
C
C  CONTENTS OF THE PO ARRAY:
C
C     WORD    NAME       DESCRIPTION                            UNITS
C  ________   _______    _______________________________________________
C      1      IVERS      VERSION NUMBER
C    2 - 3    RID        RUNOFF ZONE ID
C    4 - 8    RNAME      RUNOFF ZONE NAME
C      9      IRNUM      RUNOFF ZONE NUMBER
C     10      RLAT       LATITUDE OF RUNOFF ZONE CENTROID       DEG DEC
C     11      RLNG       LONGITUDE OF RUNOFF ZONE CENTROID      DEG DEC
C     12      RFCTR      RUNOFF ADJUSTMENT FACTOR
C     13      R24        24-HOUR API RECESSION FACTOR
C     14      PMAX       MAX LIMIT FOR NEW STORM RAIN/MELT      INCHES
C     15      ULIMW      UPPER AEI LIMIT (WINTER CURVE)         INCHES
C     16      BLIMW      BOTTOM AEI LIMIT (WINTER CURVE)        INCHES
C     17      ULIMS      UPPER AEI LIMIT (SUMMER CURVE)         INCHES
C     18      BLIMS      BOTTOM AEI LIMIT (SUMMER CURVE)        INCHES
C     19      KURV       API/AEI/FI CURVE NUMBER
C     20      IDELTA     COMPUTATIONAL TIME STEP                HOURS
C     21      NSW        NEW STORM WINDOW                       HOURS
C     22      NSPER      NUMBER OF PERIODS IN NSW
C     23      IUSEC      NUMBER OF WORDS NEEDED IN CO ARRAY
C     24      IOFAAA     I/O FLAG FOR API, AEI & FI TIME SERIES
C     25      ICOF       CARRYOVER INPUT FLAG
C   26 - 27   TSIDRM     TIME SERIES ID FOR RAIN/MELT
C     28      DTCRM      DATA TYPE CODE FOR RAIN/MELT
C   29 - 30   TSIDPE     TIME SERIES ID FOR POTENTIAL ET
C     31      DTCPE      DATA TYPE CODE FOR POTENTIAL ET
C   32 - 33   TSIDRO     TIME SERIES ID FOR RUNOFF
C     34      DTCRO      DATA TYPE CODE FOR RUNOFF
C   35 - 36   TSIDFI     TIME SERIES ID FOR FI
C     37      DTCFI      DATA TYPE CODE FOR FI
C   38 - 39   TSIDAP     TIME SERIES ID FOR API
C     40      DTCAP      DATA TYPE CODE FOR API
C   41 - 42   TSIDAE     TIME SERIES ID FOR AEI
C     43      DTCAE      DATA TYPE CODE FOR AEI
C   44 - 47              EMPTY
C
C#######################################################################
C
C
      DIMENSION PO(1),SUBNAM(2),RID(2),RNAME(5),TSIDRM(2),
     1          TSIDAE(2),TSIDRO(2),TSIDFI(2),TSIDAP(2),TSIDPE(2)
      COMMON /IONUM/ IN,IPR,IPU
      COMMON /FDBUG/ IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp35.f,v $
     . $',                                                             '
     .$Id: prp35.f,v 1.1 1995/09/17 18:50:14 dws Exp $
     . $' /
C    ===================================================================
C
      DATA SUBNAM/4hPRP3,4h5   /,NOP/35/,EMPTY/4h    /
C
C  CALL DEBUG CHECK ROUTINE
C
      CALL FPRBUG(SUBNAM,1,NOP,IFDEB)
C
C  PULL VARIABLES FROM THE PO ARRAY
C
      IVERS=PO(1)
      RID(1)=PO(2)
      RID(2)=PO(3)
      RNAME(1)=PO(4)
      RNAME(2)=PO(5)
      RNAME(3)=PO(6)
      RNAME(4)=PO(7)
      RNAME(5)=PO(8)
      IRNUM=PO(9)
      RLAT=PO(10)
      RLNG=PO(11)
      RFCTR=PO(12)
      R24=PO(13)
      PMAX=PO(14)
      ULIMW=PO(15)
      BLIMW=PO(16)
      ULIMS=PO(17)
      BLIMS=PO(18)
      KURV=PO(19)
      IDELTA=PO(20)
      NSW=PO(21)
      NSPER=PO(22)
      IUSEC=PO(23)
      IOFAAA=PO(24)
      ICOF=PO(25)
      TSIDRM(1)=PO(26)
      TSIDRM(2)=PO(27)
      DTCRM=PO(28)
      TSIDPE(1)=PO(29)
      TSIDPE(2)=PO(30)
      DTCPE=PO(31)
      TSIDRO(1)=PO(32)
      TSIDRO(2)=PO(33)
      DTCRO=PO(34)
      TSIDFI(1)=PO(35)
      TSIDFI(2)=PO(36)
      DTCFI=PO(37)
      TSIDAP(1)=PO(38)
      TSIDAP(2)=PO(39)
      DTCAP=PO(40)
      TSIDAE(1)=PO(41)
      TSIDAE(2)=PO(42)
      DTCAE=PO(43)
C
C  NOW PRINT OUT THESE VARIABLES.
C
      WRITE(IPR,1000)RNAME
      WRITE(IPR,1010)IVERS
      WRITE(IPR,1020)RID
      WRITE(IPR,1030)RNAME
      WRITE(IPR,1040)IRNUM
      WRITE(IPR,1050)RLAT
      WRITE(IPR,1060)RLNG
      WRITE(IPR,1070)RFCTR
      WRITE(IPR,1080)R24
      WRITE(IPR,1090)PMAX
      WRITE(IPR,1100)ULIMW
      WRITE(IPR,1110)BLIMW
      WRITE(IPR,1120)ULIMS
      WRITE(IPR,1130)BLIMS
      WRITE(IPR,1140)KURV
      WRITE(IPR,1150)IDELTA
      WRITE(IPR,1160)NSW
      WRITE(IPR,1170)NSPER
      WRITE(IPR,1180)IUSEC
      WRITE(IPR,1190)IOFAAA
      WRITE(IPR,1200)ICOF
      WRITE(IPR,1300)
      WRITE(IPR,1310)TSIDRM,DTCRM,IDELTA
      WRITE(IPR,1320)TSIDPE,DTCPE
      WRITE(IPR,1330)TSIDRO,DTCRO,IDELTA
      IF(TSIDFI(1)-EMPTY)340,350,340
340   WRITE(IPR,1340)TSIDFI,DTCFI,IDELTA
350   IF(TSIDAP(1)-EMPTY)360,370,360
360   WRITE(IPR,1360)TSIDAP,DTCAP,IDELTA
370   IF(TSIDAE(1)-EMPTY)380,9999,380
380   WRITE(IPR,1380)TSIDAE,DTCAE,IDELTA
9999  RETURN
1000  FORMAT(//41X,'API - HAR PARAMETER VALUES FOR ',5A4,
     1      //35X,'INTERNAL',/35X,'VARIABLE',/37X,'NAME',
     2      7X,'DESCRIPTION',30X,'CONTENTS',/)
1010  FORMAT(37X,'IVERS ',4('.'),' API - HAR VERSION NUMBER ',
     1      15('.'),1X,I4)
1020  FORMAT(37X,'RID   ',4('.'),' 8-LETTER RUNOFF ZONE I.D. ',
     1      14('.'),1X,2A4)
1030  FORMAT(37X,'RNAME ',4('.'),' 20-LETTER RUNOFF ZONE NAME ',
     1      13('.'),1X,5A4)
1040  FORMAT(37X,'IRNUM ',4('.'),' RUNOFF ZONE NUMBER ',
     1      21('.'),1X,I4)
1050  FORMAT(37X,'RLAT  ',4('.'),' LATITUDE OF R.O. ZONE CENTROID ',
     1      '(DEG DEC) ',1X,F5.2)
1060  FORMAT(37X,'RLNG  ',4('.'),' LONGITUDE OF R.O. ZONE CENTROID ',
     1      '(DEG DEC)',1X,F5.2)
1070  FORMAT(37X,'RFCTR ',4('.'),' BASIN RUNOFF ADJUSTMENT FACTOR ',
     1      9('.'),1X,F5.2)
1080  FORMAT(37X,'R24   ',4('.'),' 24-HOUR API RECESSION FACTOR ',
     1      11('.'),1X,F5.3)
1090  FORMAT(37X,'PMAX  ',4('.'),' STORM BREAK RAIN/MELT CRITERION ',
     1      8('.'),1X,F5.2)
1100  FORMAT(37X,'ULIMW ',4('.'),' UPPER LIMIT FOR AEI ',
     1      '(WINTER CURVE) ',5('.'),1X,F5.2)
1110  FORMAT(37X,'BLIMW ',4('.'),' BOTTOM LIMIT FOR AEI ',
     1      '(WINTER CURVE) ',4('.'),1X,F5.2)
1120  FORMAT(37X,'ULIMS ',4('.'),' UPPER LIMIT FOR AEI ',
     1      '(SUMMER CURVE) ',5('.'),1X,F5.2)
1130  FORMAT(37X,'BLIMS ',4('.'),' BOTTOM LIMIT FOR AEI ',
     1      '(SUMMER CURVE) ',4('.'),1X,F5.2)
1140  FORMAT(37X,'KURV  ',4('.'),' API/AEI/FI CURVE NUMBER ',
     1      16('.'),1X,I4)
1150  FORMAT(37X,'IDELTA',4('.'),' COMPUTATIONAL TIME STEP INTERVAL ',
     1      '(HOURS)',1X,I4)
1160  FORMAT(37X,'NSW   ',4('.'),' NEW STORM WINDOW (HOURS) ',
     1      15('.'),1X,I4)
1170  FORMAT(37X,'NSPER ',4('.'),' NUMBER OF PERIODS IN NSW ',
     1      15('.'),1X,I4)
1180  FORMAT(37X,'IUSEC ',4('.'),' NUMBER OF WORDS USED IN ',
     1      'THE CO ARRAY ',3('.'),1X,I4)
1190  FORMAT(37X,'IOFAAA',4('.'),' I/O FLAG FOR API, AEI & FI TIME ',
     1      'SERIES ',2X,I4,/48X,'(0 = DONT SAVE AS TS, ',
     2      '1 = SAVE AS TS)')
1200  FORMAT(37X,'ICOF  ',4('.'),' CARRYOVER INPUT FLAG ',19('.'),
     1      1X,I4,/48X,'(0 = USE DEFAULTS, 1 = READ INPUT)')
1300  FORMAT(//44X,'TIME SERIES USED BY THE API - HAR OPERATION:',
     1      //30X,'TS I.D.',7X,'TYPE',6X,'DESCRIPTION',23X,
     2      'TIME INTERVAL',/)
1310  FORMAT(30X,2A4,6X,A4,6X,'RAINFALL/MELT',21X,I4,' HOURS')
1320  FORMAT(30X,2A4,6X,A4,6X,'POTENTIAL EVAPOTRANSPIRATION',8X,
     1      '24 HOURS')
1330  FORMAT(30X,2A4,6X,A4,6X,'RUNOFF',28X,I4,' HOURS')
1340  FORMAT(30X,2A4,6X,A4,6X,'FINAL INDEX',23X,I4,' HOURS')
1360  FORMAT(30X,2A4,6X,A4,6X,'STORM API',25X,I4,' HOURS')
1380  FORMAT(30X,2A4,6X,A4,6X,'STORM AEI',25X,I4,' HOURS')
      END
