      SUBROUTINE PRP1(PL)
C.......................................
C     THIS SUBROUTINE PRINTS PARAMETER VALUES FOR THE SACRAMENTO
C        SOIL-MOISTURE ACCOUNTING OPERATION.
C.......................................
C     SUBROUTINE INITIALLY WRITTEN BY. . .
C            ERIC ANDERSON - HRL     APRIL 1979     VERSION 1
C.......................................
      DIMENSION PL(1)
      DIMENSION DET(2,2),MOPR(14)
C
C     COMMON BLOCKS.
      COMMON/FDBUG/IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
      COMMON/IONUM/IN,IPR,IPU
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp1.f,v $
     . $',                                                             '
     .$Id: prp1.f,v 1.1 1995/09/17 18:49:57 dws Exp $
     . $' /
C    ===================================================================
C
C
      DATA DET/4HUNIF,4HORM ,4HDIUR,4HNAL /
C.......................................
C     CHECK TRACE LEVEL -- TRACE LEVEL FOR THIS SUBROUTINE=1.
      IF (ITRACE.GE.1) WRITE(IODBUG,900)
  900 FORMAT (1H0,15H** PRP1 ENTERED)
C     NO DEBUG OUTPUT FOR THIS SUBROUTINE.
C.......................................
C     PRINT TITLE
      WRITE (IPR,903) (PL(I),I=3,7)
  903 FORMAT (1H0,10X,49HSACRAMENTO SOIL-MOISTURE ACCOUNTING OPERATION F
     1OR,1X,5A4)
      IDT=PL(2)
      WRITE (IPR,920) IDT
  920 FORMAT (1H0,15X,30HCOMPUTATIONAL TIME INTERVAL IS,I3,1X,6HHOURS.)
C.......................................
C     PRINT TIME SERIES USED BY THE OPERATION.
      WRITE (IPR,901)
  901 FORMAT (1H0,20X,35HTIME SERIES USED BY THIS OPERATION.)
      WRITE (IPR,902)
  902 FORMAT (1H0,15X,8HCONTENTS,14X,4HI.D.,7X,4HTYPE,5X,
     113HTIME INTERVAL)
      WRITE (IPR,904) PL(8),PL(9),PL(10),IDT
  904 FORMAT (1H0,10X,9HRAIN+MELT,16X,2A4,5X,A4,7X,I2,1X,5HHOURS)
      WRITE (IPR,905) PL(11),PL(12),PL(13),IDT
  905 FORMAT (1H ,10X,22HCHANNEL INFLOW(RUNOFF),3X,2A4,5X,A4,7X,
     1I2,1X,5HHOURS)
      JDT=24
      IET=PL(14)
      IF (IET.EQ.0) GO TO 101
      WRITE (IPR,906) PL(IET),PL(IET+1),PL(IET+2),JDT
  906 FORMAT (1H ,10X,12HPOTENTIAL ET,13X,2A4,5X,A4,7X,I2,1X,5HHOURS)
  101 J=PL(15)
      IF (J.EQ.0) GO TO 102
      ITSC=PL(J+3)
      WRITE (IPR,907) PL(J),PL(J+1),PL(J+2),ITSC
  907 FORMAT (1H ,10X,20HAREAL EXTENT OF SNOW,5X,2A4,5X,A4,7X,
     1I2,1X,5HHOURS)
  102 J=PL(16)
      IF (J.EQ.0) GO TO 103
      ITROC=PL(J+3)
      WRITE (IPR,908) PL(J),PL(J+1),PL(J+2),ITROC
  908 FORMAT (1H ,10X,17HRUNOFF COMPONENTS,8X,2A4,5X,A4,7X,
     1I2,1X,5HHOURS)
  103 J=PL(17)
      IF (J.EQ.0) GO TO 104
      ITSM=PL(J+3)
      WRITE (IPR,909) PL(J),PL(J+1),PL(J+2),ITSM
  909 FORMAT (1H ,10X,21HSOIL STORAGE CONTENTS,4X,2A4,5X,A4,
     17X,I2,1X,5HHOURS)
  104 CONTINUE
C.......................................
C     PRINT OPTIONS USED.
      J=PL(18)
      IF (J.EQ.0) GO TO 111
      WRITE (IPR,910)
  910 FORMAT (1H0,10X,43HSUMS OF WATER BALANCE VARIABLES ARE STORED.)
  111 J=PL(19)
      IF (J.EQ.0) GO TO 112
      WRITE (IPR,911)
  911 FORMAT (1H0,10X,46HDETAILED SOIL-MOISTURE OUTPUT WILL BE PRINTED.)
      IF (J.EQ.1) GO TO 112
      DO 115 I=1,14
      L=J+I
      MOPR(I)=PL(L)
      IF (MOPR(I).GT.0) GO TO 115
      L=I-1
      GO TO 116
  115 CONTINUE
      L=14
  116 L=(L/2)*2
      IF (L.EQ.0) GO TO 112
      WRITE (IPR,922) (MOPR(I),I=1,L)
  922 FORMAT (1H ,15X,10HFOR MONTHS,7(I3,1H/,I4))
  112 IF (PL(22).EQ.1.0) GO TO 113
      WRITE (IPR,921) PL(22)
  921 FORMAT (1H0,10X,46HCOMPUTATIONAL PERIOD PRECIPITATION MULTIPLIER=,
     1F5.2)
  113 CONTINUE
C.......................................
C     PRINT PARAMETER VALUES.
      WRITE (IPR,912)
  912 FORMAT (1H0,/,11X,40HPARAMETER VALUES - CAPACITIES ARE IN MM.)
      WRITE (IPR,913)
  913 FORMAT (1H0,12X,6HPX-ADJ,2X,6HPE-ADJ,3X,5HUZTWM,3X,5HUZFWM,
     15X,3HUZK,3X,5HPCTIM,3X,5HADIMP,4X,4HRIVA,5X,3HEFC,3X,
     114HDAILY ET DIST.)
      L=PL(20)
      L=L-1
      J=PL(L+19)+1.0
      L1 = L+1
      L8 = L+8
      L20 = L+20
C
C  FOLLOWING WRITE CHANGED TO CONFORM WITH VS FORTRAN LVL 66
C
C         WRITE (IPR,914) (PL(L+I),I=1,8),PL(L+20),(DET(I,J),I=1,2)
C
      WRITE (IPR,914) (PL(I),I=L1,L8),PL(L20),(DET(I,J),I=1,2)
  914 FORMAT (1H ,10X,2F8.3,2F8.0,5F8.3,4X,2A4)
      WRITE (IPR,915)
  915 FORMAT (1H0,13X,5HPBASE,3X,5HZPERC,4X,4HREXP,3X,5HLZTWM,
     13X,5HLZFSM,3X,5HLZFPM,4X,4HLZSK,4X,4HLZPK,3X,5HPFREE,3X,5HRSERV,
     14X,4HSIDE)
      PBASE=PL(L+12)*PL(L+14)+PL(L+13)*PL(L+15)
      WRITE (IPR,916) PBASE,(PL(L+I),I=9,18)
  916 FORMAT (1H ,10X,2F8.1,F8.2,3F8.0,2F8.4,3F8.2)
      WRITE (IPR,917) (I,I=1,12)
  917 FORMAT (1H0,10X,20H16TH OF MONTH VALUES,12I6)
      L=PL(21)
      L=L-1
      IF (IET.GT.0) GO TO 121
      WRITE (IPR,918) (PL(L+I),I=1,12)
  918 FORMAT (1H ,10X,16HET-DEMAND-MM/DAY,4X,12F6.1)
      GO TO 129
  121 WRITE (IPR,919) (PL(L+I),I=1,12)
  919 FORMAT (1H ,10X,13HPE-ADJUSTMENT,7X,12F6.2)
  129 IFRZE=PL(24)
      IF (IFRZE.GT.0) CALL FPRFG1(PL(IFRZE))
C.......................................
      RETURN
      END
