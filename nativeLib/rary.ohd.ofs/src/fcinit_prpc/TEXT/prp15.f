C MEMBER PRP15
C  (from old member FCPRP15)
C
      SUBROUTINE PRP15(PO)
C
C             THE FUNCTION OF THIS SUBROUTINE IS TO PRINT THE CONTENTS
C              OF THE PO ARRAY FOR THE WEIGH-TS OPERATION.
C
C             THIS SUBROUTINE WAS WRITTEN BY:
C              JAN LEWIS        HRL         MARCH,1980     VERSION NO. 1
C
      INCLUDE 'common/ionum'
C
      DIMENSION XIDT(2),XNAME(2),XID(2),PO(1),SWITCH(2)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_prpc/RCS/prp15.f,v $
     . $',                                                             '
     .$Id: prp15.f,v 1.1 1995/09/17 18:50:02 dws Exp $
     . $' /
C    ===================================================================
C
C
      DATA SWITCH/4H    ,4HAREA/
C
      WRITE(IPR,100)
      NTS=PO(2)
      WRITE(IPR,200) NTS
      WTS=PO(3)
      IF(WTS.EQ.SWITCH(1)) WRITE(IPR,300)
      IF(WTS.EQ.SWITCH(2)) WRITE(IPR,350)
      WRITE(IPR,400)
      IDT=PO(7)
      UNITS=PO(10)
      MSG=PO(9)
      NVAL=PO(8)
      WRITE(IPR,500) IDT
      WRITE(IPR,600) UNITS
      IF(MSG.EQ.0) WRITE(IPR,700)
      IF(MSG.EQ.1) WRITE(IPR,750)
      WRITE(IPR,800) NVAL
      WRITE(IPR,900)
      WRITE(IPR,1000) PO(4),PO(5)
      WRITE(IPR,1100) PO(6)
      WRITE(IPR,1200)
      IF(WTS.EQ.SWITCH(1)) WRITE(IPR,1300)
      IF(WTS.EQ.SWITCH(2)) WRITE(IPR,1350)
      K=10
      DO 10 I=1,NTS
      K=K+1
      XID(1)=PO(K)
      K=K+1
      XID(2)=PO(K)
      K=K+1
      DTYPE=PO(K)
      K=K+1
      WT=PO(K)
      IF(WTS.EQ.SWITCH(1)) GO TO 8
      K=K+1
      XNAME(1)=PO(K)
      K=K+1
      XNAME(2)=PO(K)
      WRITE(IPR,1450) I,XID,DTYPE,XNAME,WT
      GO TO 10
    8 WRITE(IPR,1400) I,XID,DTYPE,WT
   10 CONTINUE
C
  100 FORMAT(1H0,10X,28HWEIGHT TIME SERIES OPERATION)
  200 FORMAT(1H0,10X,I5,32H TIME SERIES ARE TO BE WEIGHTED.)
  300 FORMAT(1H0,10X,35HTHE WEIGHTING FACTORS WERE READ IN.)
  350 FORMAT(1H ,10X,58HTHE WEIGHTING FACTORS WERE CALCULATED USING UNIT
     1-HG AREAS.)
  400 FORMAT(1H0,10X,31HGENERAL TIME SERIES INFORMATION)
  500 FORMAT(1H0,15X,13HTIME INTERVAL,17X,I5)
  600 FORMAT(1H ,15X,5HUNITS,26X,A4)
  700 FORMAT(1H ,15X,20HMISSING DATA ALLOWED,13X,2HNO)
  750 FORMAT(1H ,15X,20HMISSING DATA ALLOWED,12X,3HYES)
  800 FORMAT(1H ,15X,24HVALUES PER TIME INTERVAL,6X,I5)
  900 FORMAT(1H0,10X,30HOUTPUT TIME SERIES INFORMATION)
 1000 FORMAT(1H0,15X,3HID.,12X,2A4)
 1100 FORMAT(1H ,15X,9HDATA TYPE,6X,A4)
 1200 FORMAT(1H0,10X,29HINPUT TIME SERIES INFORMATION)
 1300 FORMAT(1H0,15X,8HT.S. NO.,4X,3HID.,4X,4HDATA,3X,6HWEIGHT/35X,
     1 4HTYPE/)
 1350 FORMAT(1H0,15X,8HT.S. NO.,4X,3HID.,4X,4HDATA,4X,7HUNIT-HG,4X,
     1 6HWEIGHT/35X,4HTYPE,3X,9HOPERATION/)
 1400 FORMAT(1H ,17X,I3,4X,2A4,2X,A4,3X,F6.3)
 1450 FORMAT(1H ,17X,I3,4X,2A4,2X,A4,4X,2A4,3X,F6.3)
      RETURN
      END
