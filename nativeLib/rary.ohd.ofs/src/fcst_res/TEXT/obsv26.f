C MEMBER OBSV26
C  (from old member FCOBSV26)
C
      FUNCTION OBSV26(S2,IBUG)
C
C FUNCTION OBSV26 SETS IGO TO 0 IF THE POOL STORAGE (S2) AT THE END OF
C THE TIME INTERVAL IS MISSING AND SETS IGO TO 1 IF THE POOL STORAGE
C (S2) IS NOT MISSING.  OBSV26 IS USED ONLY FOR THE TIME INTERVALS UP TO
C RUN TIME.
C
C THIS FUNCTION WAS ORIGINALLY PROGRAMMED BY
C     WILLIAM E. FOX -- CONSULTING HYDROLOGIST
C     JANUARY, 1983
C
C FUNCTION OBSV26 IS IN
C
C VARIABLES IN THE ARGUMENT LIST ARE DEFINED AS FOLLOWS:
C     S2 -- STORAGE AT THE END OF THE TIME INTERVAL.
C     IBUG -- NO TRACE OR DEBUG (IBUG=0), TRACE ONLY (IBUG=1), TRACE AND
C       DEBUG (IBUG=2).
C
      COMMON/FDBUG/IODBUG,ITRACE,IDBALL,NDEBUG,IDEBUG(20)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_res/RCS/obsv26.f,v $
     . $',                                                             '
     .$Id: obsv26.f,v 1.1 1995/09/17 19:05:52 dws Exp $
     . $' /
C    ===================================================================
C
C
C WRITE TRACE AND DEBUG IF REQUIRED.
C
      IF(IBUG-1) 50,10,20
   10 WRITE(IODBUG,30)
      GO TO 50
   20 WRITE(IODBUG,30)
   30 FORMAT(1H0,10X,17H** OBSV26 ENTERED)
      WRITE(IODBUG,40) S2,IBUG
   40 FORMAT(1H0,8H S2,IBUG/1X,F12.3,I6)
C
C IGO DIRECTS THE COMPUTATIONS TO THE PROPER LOCATION IN THE OPERATIONAL
C SUBROUTINE.
C
   50 IGO=1
      IF(S2.EQ.-999.0) IGO=0
      OBSV26=IGO
      IF(IBUG-1) 100,80,60
   60 WRITE(IODBUG,70) IGO,S2,IBUG
   70 FORMAT(1H0,12H IGO,S2,IBUG/1X,I6,F12.3,I6)
   80 WRITE(IODBUG,90)
   90 FORMAT(1H0,10X,17H** LEAVING OBSV26)
  100 RETURN
      END
