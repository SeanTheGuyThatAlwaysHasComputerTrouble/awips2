C MEMBER TS1226
C  (from old member FCTS1226)
C
      SUBROUTINE TS1226(WORK,IUSEW,LEFTW,NTS12,SMALL,GATE,
     .                  LENDSU,JDEST,IERR)
C--------------------------------------------------------------------
C  ROUTINE TO GET AND CHECK TS SPECIFICATIONS FOR S/U #12 -
C   FLASHBOARDS SCHEME
C---------------------------------------------------------------------
C  JTOSTROWSKI - HRL - MARCH 1983
C----------------------------------------------------------------
C
      INCLUDE 'common/comn26'
C
C
      INCLUDE 'common/err26'
C
C
      INCLUDE 'common/fld26'
C
C
      INCLUDE 'common/read26'
C
C
      INCLUDE 'common/suky26'
C
C
      INCLUDE 'common/warn26'
C
C
C
      DIMENSION TSID(2,5),TSKEY(2,5),LKEY(5),IDT(5),DTYPE(5),IT(5),
     . TSOK(5),WORK(1)
C
      LOGICAL TSOK,ALLOK,ENDFND,NOTS,SMALL,GATE
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_res/RCS/ts1226.f,v $
     . $',                                                             '
     .$Id: ts1226.f,v 1.1 1995/09/17 18:53:18 dws Exp $
     . $' /
C    ===================================================================
C
C
      DATA TSKEY/4HLARG,4HEBDS,4HSMAL,4HLBDS,4HGATE,4H    ,
     .           4HGENQ,4H    ,4HSLUI,4HCEQ /
      DATA LKEY/2,2,1,1,2/
      DATA NKEY/5/
      DATA NDKEY/2/
C
      DATA UINT/4HINT /,DLES/4HDLES/
      DATA BLANK/4H    /
C
C  INITIALIZE LOCAL VARIABLES AND COUNTERS
C
      ALLOK = .TRUE.
      ENDFND = .FALSE.
      USEDUP = .FALSE.
C
      DO 1 I=1,5
      IT(I) = 0
      TSOK(I) = .TRUE.
    1 CONTINUE
C
      NTS12 = 0
C
C
      IERR = 0
C
C  TS FOUND, LOOKING FOR ENDT
C
      LPOS = LSPEC + NCARD + 1
      LASTCD = LENDSU
      IBLOCK = 1
C
    5 IF (NCARD .LT. LASTCD) GO TO 8
           CALL STRN26(59,1,SUKYWD(1,9),3)
           IERR = 99
           GO TO 9
    8 NUMFLD = 0
      CALL UFLD26(NUMFLD,IERF)
      IF(IERF .GT. 0 ) GO TO 9000
      NUMWD = (LEN -1)/4 + 1
      IDEST = IKEY26(CHAR,NUMWD,SUKYWD,LSUKEY,NSUKEY,NDSUKY)
      IF (IDEST.EQ.0) GO TO 5
C
C  IDEST = 9 IS FOR ENDT
C
      IF (IDEST.EQ.9.OR.IDEST.EQ.10) GO TO 9
          CALL STRN26(59,1,SUKYWD(1,9),3)
          JDEST = IDEST
          IERR = 89
    9 LENDT = NCARD
C
C  ENDT CARD OR PARMS OR CO FOUND AT LENDT,
C  ALSO ERR RECOVERY IF NEITHER ONE OF THEM FOUND.
C
C
      IBLOCK = 2
      CALL POSN26(MUNI26,LPOS)
      NCARD = LPOS - LSPEC -1
C
   10 CONTINUE
      NUMFLD = 0
      CALL UFLD26(NUMFLD,IERF)
      IF(IERF .GT. 0) GO TO 9000
      NUMWD = (LEN -1)/4 + 1
      IDEST = IKEY26(CHAR,NUMWD,TSKEY,LKEY,NKEY,NDKEY)
      IF(IDEST .GT. 0) GO TO 50
      IF (NCARD .GE. LENDT) GO TO 900
C
C  NO VALID KEYWORD FOUND
C
      CALL STER26(1,1)
      ALLOK = .FALSE.
      GO TO 10
C
C  NOW SEND CONTROL TO PROPER LOCATION FOR PROCESSING EXPECTED INPUT
C
C
   50 CONTINUE
      NPR = IDEST
      GO TO (100,200,300,400,500) , NPR
C
C----------------------------------------------------------------------
C  'LARGEBDS' FOUND. GET THE TS INFO FROM THE REST OF THE LINE.
C   IF NOT FOUND, TS IS NOT TO BE USED. ONLY BLANKS WILL BE STORED.
C
  100 CONTINUE
      IT(1) = IT(1) + 1
      IF (IT(1).GT.1) CALL STER26(39,1)
C
      CALL TSID26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),TSOK(NPR))
      IF (.NOT.TSOK(NPR)) GO TO 10
C
C  TIME INTERVAL MUST BE EQUAL TO THE OPERATION TIME INTERVAL.
C
  130 CONTINUE
      IF (IDT(NPR).EQ.MINODT) GO TO 140
C
      CALL STER26(48,1)
      GO TO 10
C
C  SEE IF TIME SERIES EXISTS
C
  140 CONTINUE
      CALL CHEKTS(TSID(1,NPR),DTYPE(NPR),IDT(NPR),0,DCODE,1,1,IERCK)
      IF (IERCK.EQ.0) GO TO 145
C
      CALL STER26(45,1)
      GO TO 10
C
C  CHECK UNITS FOR THIS TIME-SERIES.
C
  145 CONTINUE
      CALL FDCODE(DTYPE(NPR),UCODE,X,IX,IX,X,IX,IERD)
      IF (UCODE.EQ.UINT) GO TO 150
C
      CALL STRN26(93,1,UCODE,1)
      GO TO 10
C
C  CHECK DIMENSIONS AGAINST ALLOWED FOR THIS TIME SERIES.
C
  150 CONTINUE
      IF (DCODE.EQ.DLES) GO TO 160
C
      CALL STER26(46,1)
      GO TO 10
C
C  SEE IF THIS TIME SERIES WAS ALREADY DEFINED, AND ADD ID TO LIST
C  OF INPUT TIME-SERIES IF IT'S NEW.
C
  160 CONTINUE
      IO = 0
      CALL MLTS26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),IO,IERM)
      IF (IERM.GT.0) GO TO 10
C
C  INPUT IS OK
C
  190 CONTINUE
      TSOK(NPR) = .TRUE.
      GO TO 10
C
C----------------------------------------------------------------------
C  'SMALLBDS' FOUND. GET THE TS INFO FROM THE REST OF THE LINE.
C   IF NOT FOUND, TS IS NOT TO BE USED. ONLY BLANKS WILL BE STORED.
C
  200 CONTINUE
      IT(NPR) = IT(NPR) + 1
      IF (IT(NPR).GT.1) CALL STER26(39,1)
      IF (.NOT.SMALL) CALL STRN26(60,1,TSKEY(1,NPR),LKEY(NPR))
C
      CALL TSID26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),TSOK(NPR))
      IF (.NOT.TSOK(NPR)) GO TO 10
C
C  TIME INTERVAL MUST BE EQUAL TO THE OPERATION TIME INTERVAL.
C
  230 CONTINUE
      IF (IDT(NPR).EQ.MINODT) GO TO 240
C
      CALL STER26(48,1)
      GO TO 10
C
C  SEE IF TIME SERIES EXISTS
C
  240 CONTINUE
      CALL CHEKTS(TSID(1,NPR),DTYPE(NPR),IDT(NPR),0,DCODE,1,1,IERCK)
      IF (IERCK.EQ.0) GO TO 245
C
      CALL STER26(45,1)
      GO TO 10
C
C  CHECK UNITS FOR THIS TIME-SERIES.
C
  245 CONTINUE
      CALL FDCODE(DTYPE(NPR),UCODE,X,IX,IX,X,IX,IERD)
      IF (UCODE.EQ.UINT) GO TO 250
C
      CALL STRN26(93,1,UCODE,1)
      GO TO 10
C
C  CHECK DIMENSIONS AGAINST ALLOWED FOR THIS TIME SERIES.
C
  250 CONTINUE
      IF (DCODE.EQ.DLES) GO TO 260
C
      CALL STER26(46,1)
      GO TO 10
C
C  SEE IF THIS TIME SERIES WAS ALREADY DEFINED, AND ADD ID TO LIST
C  OF INPUT TIME-SERIES IF IT'S NEW.
C
  260 CONTINUE
      IO = 0
      CALL MLTS26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),IO,IERM)
      IF (IERM.GT.0) GO TO 10
C
C  INPUT IS OK
C
  290 CONTINUE
      TSOK(NPR) = .TRUE.
      GO TO 10
C
C----------------------------------------------------------------------
C  'GATE' FOUND. GET THE TS INFO FROM THE REST OF THE LINE.
C   IF NOT FOUND, TS IS NOT TO BE USED. ONLY BLANKS WILL BE STORED.
C
  300 CONTINUE
      IT(NPR) = IT(NPR) + 1
      IF (IT(NPR).GT.1) CALL STER26(39,1)
      IF (.NOT.GATE) CALL STRN26(60,1,TSKEY(1,NPR),LKEY(NPR))
C
      CALL TSID26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),TSOK(NPR))
      IF (.NOT.TSOK(NPR)) GO TO 10
C
C  TIME INTERVAL MUST BE EQUAL TO THE OPERATION TIME INTERVAL.
C
  330 CONTINUE
      IF (IDT(NPR).EQ.MINODT) GO TO 340
C
      CALL STER26(48,1)
      GO TO 10
C
C  SEE IF TIME SERIES EXISTS
C
  340 CONTINUE
      CALL CHEKTS(TSID(1,NPR),DTYPE(NPR),IDT(NPR),0,DCODE,1,1,IERCK)
      IF (IERCK.EQ.0) GO TO 345
C
      CALL STER26(45,1)
      GO TO 10
C
C  CHECK UNITS FOR THIS TIME-SERIES.
C
  345 CONTINUE
      CALL FDCODE(DTYPE(NPR),UCODE,X,IX,IX,X,IX,IERD)
      IF (UCODE.EQ.UM) GO TO 350
C
      CALL STRN26(93,1,UCODE,1)
      GO TO 10
C
C  CHECK DIMENSIONS AGAINST ALLOWED FOR THIS TIME SERIES.
C
  350 CONTINUE
      IF (DCODE.EQ.DIML) GO TO 360
C
      CALL STER26(46,1)
      GO TO 10
C
C  SEE IF THIS TIME SERIES WAS ALREADY DEFINED, AND ADD ID TO LIST
C  OF INPUT TIME-SERIES IF IT'S NEW.
C
  360 CONTINUE
      IO = 0
      CALL MLTS26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),IO,IERM)
      IF (IERM.GT.0) GO TO 10
C
C  INPUT IS OK
C
  390 CONTINUE
      TSOK(NPR) = .TRUE.
      GO TO 10
C
C----------------------------------------------------------------------
C  'GENQ' FOUND. GET THE TS INFO FROM THE REST OF THE LINE.
C   IF NOT FOUND, TS IS NOT TO BE USED. ONLY BLANKS WILL BE STORED.
C
  400 CONTINUE
      IT(NPR) = IT(NPR) + 1
      IF (IT(NPR).GT.1) CALL STER26(39,1)
C
      CALL TSID26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),TSOK(NPR))
      IF (.NOT.TSOK(NPR)) GO TO 10
C
C  TIME INTERVAL MUST BE EQUAL TO THE OPERATION TIME INTERVAL.
C
  430 CONTINUE
      IF (IDT(NPR).EQ.MINODT) GO TO 440
C
      CALL STER26(48,1)
      GO TO 10
C
C  SEE IF TIME SERIES EXISTS
C
  440 CONTINUE
      CALL CHEKTS(TSID(1,NPR),DTYPE(NPR),IDT(NPR),0,DCODE,1,1,IERCK)
      IF (IERCK.EQ.0) GO TO 445
C
      CALL STER26(45,1)
      GO TO 10
C
C  CHECK UNITS FOR THIS TIME-SERIES.
C
  445 CONTINUE
      CALL FDCODE(DTYPE(NPR),UCODE,X,IX,IX,X,IX,IERD)
      IF (UCODE.EQ.UCMSD) GO TO 450
C
      CALL STRN26(93,1,UCODE,1)
      GO TO 10
C
C  CHECK DIMENSIONS AGAINST ALLOWED FOR THIS TIME SERIES.
C
  450 CONTINUE
      IF (DCODE.EQ.DIML3) GO TO 460
C
      CALL STER26(46,1)
      GO TO 10
C
C  SEE IF THIS TIME SERIES WAS ALREADY DEFINED, AND ADD ID TO LIST
C  OF INPUT TIME-SERIES IF IT'S NEW.
C
  460 CONTINUE
      IO = 0
      CALL MLTS26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),IO,IERM)
      IF (IERM.GT.0) GO TO 10
C
C  INPUT IS OK
C
  490 CONTINUE
      TSOK(NPR) = .TRUE.
      GO TO 10
C
C----------------------------------------------------------------------
C  'SLUICEQ' FOUND. GET THE TS INFO FROM THE REST OF THE LINE.
C   IF NOT FOUND, TS IS NOT TO BE USED. ONLY BLANKS WILL BE STORED.
C
  500 CONTINUE
      IT(NPR) = IT(NPR) + 1
      IF (IT(NPR).GT.1) CALL STER26(39,1)
C
      CALL TSID26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),TSOK(NPR))
      IF (.NOT.TSOK(NPR)) GO TO 10
C
C  TIME INTERVAL MUST BE EQUAL TO THE OPERATION TIME INTERVAL.
C
  530 CONTINUE
      IF (IDT(NPR).EQ.MINODT) GO TO 540
C
      CALL STER26(48,1)
      GO TO 10
C
C  SEE IF TIME SERIES EXISTS
C
  540 CONTINUE
      CALL CHEKTS(TSID(1,NPR),DTYPE(NPR),IDT(NPR),0,DCODE,1,1,IERCK)
      IF (IERCK.EQ.0) GO TO 545
C
      CALL STER26(45,1)
      GO TO 10
C
C  CHECK UNITS FOR THIS TIME-SERIES.
C
  545 CONTINUE
      CALL FDCODE(DTYPE(NPR),UCODE,X,IX,IX,X,IX,IERD)
      IF (UCODE.EQ.UCMSD) GO TO 550
C
      CALL STRN26(93,1,UCODE,1)
      GO TO 10
C
C  CHECK DIMENSIONS AGAINST ALLOWED FOR THIS TIME SERIES.
C
  550 CONTINUE
      IF (DCODE.EQ.DIML3) GO TO 560
C
      CALL STER26(46,1)
      GO TO 10
C
C  SEE IF THIS TIME SERIES WAS ALREADY DEFINED, AND ADD ID TO LIST
C  OF INPUT TIME-SERIES IF IT'S NEW.
C
  560 CONTINUE
      IO = 0
      CALL MLTS26(TSID(1,NPR),DTYPE(NPR),IDT(NPR),IO,IERM)
      IF (IERM.GT.0) GO TO 10
C
C  INPUT IS OK
C
  590 CONTINUE
      TSOK(NPR) = .TRUE.
      GO TO 10
C
C------------------------------------------------------------------
C  'ENDT' FOUND. STORE TS INFO IF INPUT WAS ERROR FREE
C
  900 CONTINUE
C
      DO 910 I =1,3
           IF (.NOT.TSOK(I)) GO TO 9999
  910 CONTINUE
C
C
C  IF ERRORS OCCURRED, JUST EXIT
C
      IF (.NOT.ALLOK) GO TO 9999
C
      DO 1020 NPR = 1,5
C
C  IF THE TIME-SERIES IS NOT TO BE USED, JUST STORE AN ALL BLANK ID
C  IN 'WORK'.
C
      IF (IT(NPR).EQ.0) GO TO 1015
C
      DT = IDT(NPR) + 0.01
      CALL FLWK26(WORK,IUSEW,LEFTW,TSID(1,NPR),501)
      CALL FLWK26(WORK,IUSEW,LEFTW,TSID(2,NPR),501)
      CALL FLWK26(WORK,IUSEW,LEFTW,DTYPE(NPR),501)
      CALL FLWK26(WORK,IUSEW,LEFTW,DT,501)
      CALL FLWK26(WORK,IUSEW,LEFTW,0.01,501)
C
      NTS12 = NTS12 + 5
      GO TO 1020
C
C  STORE ALL BLANK ID IF TS IS NOT USED.
C
 1015 CONTINUE
      CALL FLWK26(WORK,IUSEW,LEFTW,BLANK,501)
      CALL FLWK26(WORK,IUSEW,LEFTW,BLANK,501)
      NTS12 = NTS12 + 2
C
 1020 CONTINUE
      GO TO 9999
C
C--------------------------------------------------------------
C  ERROR IN UFLD26
C
 9000 CONTINUE
      IF (IERF.EQ.1) CALL STER26(19,1)
      IF (IERF.EQ.2) CALL STER26(20,1)
      IF (IERF.EQ.3) CALL STER26(21,1)
      IF (IERF.EQ.4) CALL STER26( 1,1)
C
      IF (NCARD.GE.LASTCD) GO TO 9100
      IF (IBLOCK.EQ.1)  GO TO 5
      IF (IBLOCK.EQ.2)  GO TO 10
C
 9100 USEDUP = .TRUE.
C
 9999 CONTINUE
      RETURN
      END
