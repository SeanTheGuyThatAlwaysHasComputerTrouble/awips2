C MEMBER CMPS26
C  (from old member FCCMPS26)
C
C DESC TRANSLATE AND STORE INTO THE WORK ARRAY THE COMPARISON VARIABLES
C-----------------------------------------------------------------------
C
      SUBROUTINE CMPS26(WORK,IUSEW,LEFTW,NWDCMP)
C
C-----------------------------------------------------------------------
C
C  ARGS:
C
C      WORK - ARRAY TO HOLD FINAL FORMAT OF COMPARISON VARIABLES
C     IUSEW - NUMBER OF WORDS ALREADY USED IN WORK ARRAY,
C     LEFTW - NUMBER OF WORDS LEFT IN WORK ARRAY
C    NWDCMP - NUMBER OF WORDS USED TO STORE COMPARISON VARIABLES
C
C
C  COMPARISON VARIABLES HAVE BEEN READ IN RCL AND STORED IN /CMPV26/
C
C-----------------------------------------------------------------------
C
C  JTOSTROWSKI - HRL - MARCH 1983
C----------------------------------------------------------------
      INCLUDE 'common/cmpv26'
C
      DIMENSION WORK(1)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcinit_res/RCS/cmps26.f,v $
     . $',                                                             '
     .$Id: cmps26.f,v 1.2 1999/04/22 14:42:03 page Exp $
     . $' /
C    ===================================================================
C
C
      NWDCMP = 1
C
C  STORE NUM. OF COMPARISON VARIABLES IN WORK
C
      CMPNUM = NUMCMP + 0.01
      CALL FLWK26(WORK,IUSEW,LEFTW,CMPNUM,501)
C
C  IF NO COMPARISON VARIABLES HAVE BEEN USED, NO NEED TO STORE THEM
C
      IF (NUMCMP.EQ.0) GO TO 99
C
C  LOOP THROUGH ALL OF THE DEFINED COMPARISON VARIABLES
C
      DO 10 I=1,NUMCMP
      ITP = ICTARY(I)
      GO TO (100,200,300), ITP
C
C  COMP VARIABLE TYPE IS 1. FORMAT TO BE STORED IS TYPE NUMBER, VALUE,
C  AND TYPE OF COMPARISON BEING MADE. NUMBER OF WORDS TO STORE THIS KIND
C   IS 3.
C
  100 CONTINUE
      CALL FLWK26(WORK,IUSEW,LEFTW,1.01,501)
      CALL FLWK26(WORK,IUSEW,LEFTW,CMPVAL(I),501)
      CMPTYP = ICMPTY(I) + 0.01
      CALL FLWK26(WORK,IUSEW,LEFTW,CMPTYP,501)
      NWDCMP = NWDCMP + 3
      GO TO 10
C
C  COMP VARIABLE TYPE IS 2. FORMAT TO BE STORED IS TYPE, 12-CHARACTER
C  NAME, THE VALUE, AND THE TYPE OF COMPARISON BEING MADE.
C  NUMBER OF WORDS NEEDED TO STORE THIS TYPE IS 6.
C
  200 CONTINUE
      CALL FLWK26(WORK,IUSEW,LEFTW,2.01,501)
      DO 210 J=1,3
      CALL FLWK26(WORK,IUSEW,LEFTW,CMNAME(J,I),501)
  210 CONTINUE
      CALL FLWK26(WORK,IUSEW,LEFTW,CMPVAL(I),501)
      CMPTYP = ICMPTY(I) + 0.01
      CALL FLWK26(WORK,IUSEW,LEFTW,CMPTYP,501)
      NWDCMP = NWDCMP + 6
      GO TO 10
C
C  COMP VARIABLE IS TYPE 3. FORMAT TO BE STORED IS TYPE, THEN CODE FOR
C  FUNCTION, CODE FOR OPERATOR, AND CODE FOR FACTOR VALUE. NUMBER OF
C  WORDS TO STORE THIS TYPE IS 4.
C
  300 CONTINUE
      CALL FLWK26(WORK,IUSEW,LEFTW,3.01,501)
      DO 310 J=1,3
      CALL FLWK26(WORK,IUSEW,LEFTW,CMNAME(J,I),501)
  310 CONTINUE
      NWDCMP = NWDCMP + 4
C
   10 CONTINUE
C
   99 CONTINUE
      RETURN
      END
