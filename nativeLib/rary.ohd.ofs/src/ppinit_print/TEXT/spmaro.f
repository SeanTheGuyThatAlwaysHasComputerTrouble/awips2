C MEMBER SPMARO
C-----------------------------------------------------------------------
C
C DESC PRINT MEAN AREAL RUNOFF (MARO) PARAMETERS
C
      SUBROUTINE SPMARO (IVMARO,XMAROI,XDESCR,CENTRD,NGRID,
     *   ITSREC,NMSGPA,XSTAID,MMDRBX,MDRGPA,NMGPA,NMRFRO,UNUSED,
     *   ISTAT)
C
      REAL XMAPI/4HMAPI/,XMAPG/4HMAPG/
      REAL XPRNT(2)/2*4H    /
C
      DIMENSION NCOL(3),NROW(3)
C
      INCLUDE 'scommon/dimmaro'
      DIMENSION UNUSED(1)
C
      INCLUDE 'uio'
      INCLUDE 'scommon/sudbgx'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/ppinit_print/RCS/spmaro.f,v $
     . $',                                                             '
     .$Id: spmaro.f,v 1.1 1995/09/17 19:14:13 dws Exp $
     . $' /
C    ===================================================================
C
C
C
      IF (ISTRCE.GT.0) WRITE (IOSDBG,100)
      IF (ISTRCE.GT.0) CALL SULINE (IOSDBG,1)
C
C  SET DEBUG LEVEL
      LDEBUG=ISBUG(4HMARO)
C
      ISTAT=0
C
C  CHECK NUMBER OF LINES LEFT ON PAGE
10    IF (ISLEFT(5).GT.0) CALL SUPAGE
C
C  PRINT HEADING
      WRITE (LP,110)
      CALL SULINE (LP,2)
      WRITE (LP,120)
      CALL SULINE (LP,2)
      WRITE (LP,130)
      CALL SULINE (LP,1)
C
C  PRINT PARAMETER ARRAY VERSION NUMBER
      IF (LDEBUG.EQ.0) GO TO 20
         WRITE (LP,140) IVMARO
         CALL SULINE (LP,2)
C
C  PRINT MARO AREA IDENTIFIER, DESCRIPTIVE INFORMATION AND CENTROID
C  COORDINATES
20    WRITE (LP,150) XMAROI,XDESCR,CENTRD
      CALL SULINE (LP,2)
C
      IF (LDEBUG.EQ.0) GO TO 30
C
C  PRINT NUMBER OF GRID POINTS IN THE AREA
      WRITE (LP,160) NGRID
      CALL SULINE (LP,2)
C
C  PRINT FIRST ORDER STAION IDENTIFER AND GRID POINT ADDRESS
30    WRITE (LP,170) XSTAID,NMSGPA
      CALL SULINE (LP,2)
C
C  PRINT MDR BOX NUMBERS OF 3 BOXES ASSIGNED TO THE AREA
      DO 40 I=1,3
         CALL SBNUMM (MMDRBX(I),NCOL(I),NROW(I),IERR)
40       CONTINUE
      WRITE (LP,190) (NROW(I),NCOL(I),I=1,3)
      CALL SULINE (LP,2)
C
C  PRINT GRID POINT ADDRESSES OF THE CENTROID OF THE THREE MDR BOXES
      WRITE (LP,200) MDRGPA
      CALL SULINE (LP,2)
C
C  PRINT GRID POINT ADDRESS AND RAINFALL-RUNOFF RELATIONSHIP NUMBER
C  OF EACH GRID POINT IN THE MARO AREA
      WRITE (LP,210)
      CALL SULINE (LP,2)
      WRITE (LP,220)
      CALL SULINE (LP,3)
      DO 50 I=1,NGRID
         IF (ISNWPG(LP).EQ.1) WRITE (LP,220)
         IF (ISNWPG(LP).EQ.1) CALL SULINE (LP,3)
         WRITE (LP,230) I,NMGPA(I),NMRFRO(I)
         CALL SULINE (LP,1)
50       CONTINUE
C
C  PRINT RECORD NUMBERS OF THE MAPG AND MAPI TIME SERIES HEADERS
      IF (LDEBUG.GT.0) WRITE (IOSDBG,240) ITSREC
      IF (LDEBUG.GT.0) CALL SULINE (IOSDBG,1)
      IF (ITSREC(1).GT.0.AND.ITSREC(2).GT.0) GO TO 60
         NUM=0
         IF (ITSREC(1).EQ.0) NUM=NUM+1
         IF (ITSREC(1).EQ.0) XPRNT(NUM)=XMAPG
         IF (ITSREC(2).EQ.0) NUM=NUM+1
         IF (ITSREC(2).EQ.0) XPRNT(NUM)=XMAPI
         WRITE (LP,260) XPRNT
         CALL SULINE (LP,2)
         GO TO 70
60    WRITE (LP,250)
      CALL SULINE (LP,2)
C
C  PRINT NUMBER OF UNUSED POSITIONS
70    NUNUSD=2
      IF (LDEBUG.GT.0) WRITE (LP,180) NUNUSD
      IF (LDEBUG.GT.0) CALL SULINE (LP,2)
C
      WRITE (LP,110)
      CALL SULINE (LP,2)
C
80    IF (LDEBUG.GT.0.AND.ISTAT.EQ.0) WRITE (IOSDBG,270) XMAROI
      IF (LDEBUG.GT.0.AND.ISTAT.EQ.0) CALL SULINE (IOSDBG,1)
C
90    IF (ISTRCE.GT.0) WRITE (IOSDBG,280)
      IF (ISTRCE.GT.0) CALL SULINE (IOSDBG,1)
C
      RETURN
C
C- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
C
100   FORMAT (' *** ENTER SPMARO')
110   FORMAT (1H0,132(1H-))
120   FORMAT ('0*-->  MARO AREA PARAMETERS')
130   FORMAT (1H )
140   FORMAT ('0PARAMETER ARRAY VERSION NUMBER = ',I2)
150   FORMAT ('0MARO AREA ID = ',2A4,8X,'DESCRIPTION = ',5A4,8X,
     *   'CENTROID: LAT=',F6.2,2X,'LON=',F7.2)
160   FORMAT ('0NUMBER OF GRID POINTS IN MARO AREA = ',I4)
170   FORMAT ('0FIRST ORDER STATION IDENTIFIER = ',2A4,5X,
     *   'GRID POINT ADDRESS = ',I5)
180   FORMAT ('0NUMBER OF UNUSED POSITIONS = ',I2)
190   FORMAT ('0MDR BOX NUMBERS OF 3 MDR BOXES ASSIGNED TO THE ',
     *   'AREA (ROW,COL)= ',6(I3,',',I3,3X))
200   FORMAT ('0GRID POINT ADDRESS OF 3 MDR BOXES ASSIGNED TO THE ',
     *   'AREA = ',3(I5,1X))
210   FORMAT ('0GRID POINT ADDRESS AND RAINFALL-RUNOFF RELATIONSHIP ',
     *   'NUMBER OF EACH GRID POINT IN THE AREA:')
220   FORMAT (1H0,T20,'GPA ADDRESS',5X,'RFRO NUMBER' /
     *       T20,'-----------',5X,'-----------')
230   FORMAT (T16,I3,1X,3X,I5,3X,5X,3X,I5)
240   FORMAT (' ITSREC(1)=',I5,3X,'ITSREC(2)=',I5)
250   FORMAT ('0TIME SERIES TYPES NOT TO BE GENERATED = -NONE-')
260   FORMAT ('0TIME SERIES TYPES NOT TO BE GENERATED = ',A4,1X,A4)
270   FORMAT (' *** NOTE - MARO PARAMETERS FOR AREA ',2A4,
     *   ' SUCCESSFULLY PRINTED.')
280   FORMAT (' *** EXIT SPMARO')
C
      END
