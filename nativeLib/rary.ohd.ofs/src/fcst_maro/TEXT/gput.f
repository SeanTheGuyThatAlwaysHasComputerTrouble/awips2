C MEMBER GPUT
C  (from old member PPGPUT)
C
      SUBROUTINE GPUT(IPAIR, IWORK, IREC, IGRID, NUM)
C
C.....THIS SUBROUTINE BREAKS OUT THE COMPONENT PARTS OF THE OG24
C.....PARAMETRIC ARRAY. THE ARRAY ORIGINALLY CONSISTS OF PAIRS
C.....OF GRID POINT ADDRESSES AND RECORD POINTERS FOR ALL STATIONS
C.....FOR WHICH NWS COMMS HB 5 IDS EXIST. THIS SUBROUTINE BREAKS
C.....OUT THE GRID POINT ADDRESS ARRAY AND THE RECORD POINTER ARRAY
C.....SO THEY CAN BE ADDRESSED INDIVIDUALLY.
C
C.....THE ARGUMENT LIST TO THE SUBROUTINE IS AS FOLLOWS:
C
C.....IPAIR   - AN ARRAY OF UNSORTED PAIRS OF GRID POINT ADDRESSES AND
C.....          RECORD POINTERS FOR THOSE ENTRIES IN THE NAMED
C.....          STATION NETWORK.
C.....IWORK   - A WORK ARRAY TO MOVE THE UNSORTED PAIRS INTO.
C.....IREC    - THE ARRAY OF RECORD POINTERS.
C.....IGRID   - THE ARRAY OF GRID POINT ADDRESSES.
C.....NUM     - THE NUMBER OF ENTRIES TO BE PROCESSED.
C
C.....ORIGINALLY WRITTEN BY:
C
C.....JERRY M. NUNN       WGRFC FT. WORTH, TEXAS       OCTOBER 8, 1987
C
      INTEGER*2 IPAIR(1), IWORK(1), IREC(1), IGRID(1)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_maro/RCS/gput.f,v $
     . $',                                                             '
     .$Id: gput.f,v 1.1 1995/09/17 19:02:20 dws Exp $
     . $' /
C    ===================================================================
C
C
C.....MOVE THE UNSORTED PAIRS INTO THE TEMPORARY WORK AREA. WE ARE
C.....GOING TO WRITE OVER THIS AREA.
C
      NP2 = NUM*2
C
      DO 10 NP = 1, NP2
      IWORK(NP) = IPAIR(NP)
   10 CONTINUE
C
C.....BREAK OUT THE ARRAY OF RECORD POINTERS.
C
      DO 20 NP = 1, NUM
      KP = NP*2 - 1
      IREC(NP) = IWORK(KP)
   20 CONTINUE
C
C.....NOW BREAK OUT THE ARRAY OF GRID POINT ADDRESSES.
C
      DO 30 NP = 1, NUM
      KP = NP*2
      IGRID(NP) = IWORK(KP)
   30 CONTINUE
C
      RETURN
      END
