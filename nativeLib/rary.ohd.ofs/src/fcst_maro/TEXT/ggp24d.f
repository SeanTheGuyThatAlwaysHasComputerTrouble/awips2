C MEMBER GGP24D
C  (from old member PPGGP24D)
C
      SUBROUTINE GGP24D(NSTA, NSHARE, IGPA, IPPTR)
C
C.....THIS SUBROUTINE DUMPS OUT THE CONTENTS OF THE GP24 PARAMETRIC
C.....ARRAY.  HERE ARE THE ARGUMENTS:
C
C.....NSTA   - NUMBER OF STATIONS IN THE GP24 ARRAY.
C.....NSHARE - NUMBER OF STATIONS THAT SHARE THE SAME GRID POINT ADRS.
C.....IGPA   - GRID POINT ADRS/SHARED GRID POINT ADRS PAIRS.
C.....IPPTR  - PP24 POINTERS FOR STATIONS THAT SHARE THE SAME GRID POINT
C.....         ADDRESS.
C
C.....JERRY M. NUNN       WGRFC FT. WORTH, TEXAS       NOVEMBER 19, 1987
C
      INTEGER*2 IGPA(1), IPPTR(1), IGRID, IPTR, NUM, LOC, IBGN, IEND
C
      INCLUDE 'common/pudbug'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_maro/RCS/ggp24d.f,v $
     . $',                                                             '
     .$Id: ggp24d.f,v 1.1 1995/09/17 19:01:43 dws Exp $
     . $' /
C    ===================================================================
C
C
  900 FORMAT(1H0, '*** ENTER SUBROUTINE GGP24D ***')
  901 FORMAT(1H0, '*** EXIT SUBROUTINE GGP24D ***')
  902 FORMAT(1H0, 'GP24 PARAMETRIC ARRAY DUMP', //, 1X, 'NUMBER OF STATI
     *ONS IN LIST = ', I5, 3X, 'NUMBER OF SHARED GRID POINT ADDRESSES =
     *', I5)
  903 FORMAT(1H0, 'NOTE:  THE NUMBER (IN COL. 1) REFERS TO THE LOCATION
     *IN THE PP24 DATA ARRAY.  THE SHARED GRID ARRAY POINTER (COL. 3) CO
     *NTAINS THE', /, 8X, 'NUMBER OF STATIONS THAT SHARE THIS GRID POINT
     * AND A POINTER TO A BEGINNING LOCATION IN THE SHARED DATA ARRAY.
     *THE SHARED', /, 8X, 'DATA ARRAY (BEGINNING IN COL. 6) CONTAINS POI
     *NTERS TO LOCATIONS IN THE PP24 DATA ARRAY.')
  904 FORMAT(1H0, 8X, 'GRID PT  SHARED GRID    S H A R E D   G R I D   A
     * R R A Y', /, 1X, 'NUMBER  ADDRESS  ARRAY POINTER  PP24 ARRAY POSI
     *TIONS OF OTHER STATIONS THAT SHARE THE SAME GRID ADDRESS')
  905 FORMAT(1X, I5, '.', 3X, I6, 5X, I6, 6X, 'LOCATIONS ', I5, ' THRU '
     *, I5, 3X, 9(2X, I5))
  906 FORMAT(1X, I5, '.', 3X, I6, 5X, I6)
C
      IF(IPTRCE .GE. 3) WRITE(IOPDBG,900)
      WRITE(IOPDBG,902) NSTA, NSHARE
      WRITE(IOPDBG,903)
      WRITE(IOPDBG,904)
C
C.....LOOP THRU THE NUMBER OF STATIONS.
C
      DO 100 NP = 1, NSTA
      KP     = 2*NP - 1
      LP     = KP + 1
      IGRID  = IGPA(KP)
      IPTR   = IGPA(LP)
      JP     = IPTR
      LOC    = IABS(JP/10)
      NUM    = IABS(JP) - LOC*10
      IF(NUM .LE. 1) GOTO 50
      IBGN   = LOC
      IEND   = LOC + NUM - 1
      WRITE(IOPDBG,905) NP, IGRID, IPTR, IBGN, IEND,
     * (IPPTR(KP), KP = IBGN, IEND)
      GOTO 100
C
   50 WRITE(IOPDBG,906) NP, IGRID, IPTR
  100 CONTINUE
C
      IF(IPTRCE .GE. 3) WRITE(IOPDBG,901)
      RETURN
      END
