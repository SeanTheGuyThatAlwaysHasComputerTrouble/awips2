C MEMBER GPLVIT
C  (from old member PPGPLVIT)
C
      SUBROUTINE GPLVIT(IGRID, JGRID, PCPN1, PCPN2, IFLAG, JFLAG,
     * CL1, CL2, NUMID, DESC1, DESC2, NUMDSC)
C
C.....THIS SUBROUTINE INITIALIZES VALUES IN THE OUTPUT PRINT LINE ON
C.....THE PRECIPITATION STATION PRINTOUT ROUTINE.
C
C.....THE PRECIPITATION LISTS CONSISTS OF TWO COLUMNS OF 24-HOURLY
C.....PRECIPITATION NWS IDS, NAMES, GRID POINT ADDRESSES, AND
C.....PRECIPITATION AMOUNTS.  THE LEFT-HAND LIST IS SORTED IN DESCENDING
C.....ORDER BY PRECIPITATION AMOUNTS, AND THE RIGHT-HAND LIST IS SORTED
C.....IN ASCENDING ALPHABETICAL ORDER BY COMMON (PLAIN LANGUAGE) STATION
C.....DESCRIPTION.
C
C.....HERE ARE THE ARGUMENTS:
C
C.....IGRID  - GRID POINT ADDRESS, LEFT-HAND COLUMN.
C.....JGRID  - GRID POINT ADDRESS, RIGHT-HAND COLUMN.
C.....PCPN1  - PRECIP AMOUNT, LEFT-HAND COLUMN.
C.....PCPN2  - PRECIP AMOUNT, RIGHT-HAND COLUMN.
C.....IFLAG  - AN OBSERVED DATA FLAG FOR THE LEFT-HAND COLUMN.
C.....JFLAG  - AN OBSERVED DATA FLAG FOR THE RIGHT-HAND COLUMN.
C.....CL1    - NWS CALL LETTER SET, LEFT-HAND COLUMN.
C.....CL2    - NWS CALL LETTER SET, RIGHT-HAND COLUMN.
C.....NUMID  - NUMBER OF WORDS IN CL1 AND CL2.
C.....DESC1  - STATION DESCRIPTION, LEFT-HAND COLUMN.
C.....DESC2  - STATION DESCRIPTION, RIGHT-HAND COLUMN.
C.....NUMDSC - NUMBER OF WORDS IN DESC1 AND DESC2.
C
C.....ORIGINALLY WRITTEN BY:
C
C.....JERRY M. NUNN       WGRFC FT. WORTH, TEXAS       DECEMBER 1986
C
C
      DIMENSION CL1(1), CL2(1), DESC1(1), DESC2(1)
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/ofs/src/fcst_maro/RCS/gplvit.f,v $
     . $',                                                             '
     .$Id: gplvit.f,v 1.1 1995/09/17 19:02:15 dws Exp $
     . $' /
C    ===================================================================
C
C
      DATA BLANK, NBLNK /4h    , 1h /
C
C
C.....INITIALIZE THE VARIABLES IN THE PRINT LINE.
C
      IGRID = 0
      JGRID = 0
      PCPN1  = 0.0
      PCPN2  = 0.0
      IFLAG = NBLNK
      JFLAG = NBLNK
C
      DO 10 KP = 1, NUMID
      CL1(KP) = BLANK
      CL2(KP) = BLANK
   10 CONTINUE
C
      DO 20 KP = 1, NUMDSC
      DESC1(KP) = BLANK
      DESC2(KP) = BLANK
   20 CONTINUE
C
      RETURN
      END
