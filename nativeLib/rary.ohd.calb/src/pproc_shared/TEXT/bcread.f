C MEMBER BCREAD
C
C***********************************************************************
C
      SUBROUTINE BCREAD(NBASIN,RKEY1,MULT,RKEY2,IT,RKEY4,RKEY6,IMOW,
     * IMOS,RKEY7,RKEY8,RKEY9,STMNWT,RKEY10,ISTRT,NFLD,ISTAT,IERR)
      DIMENSION UCHAR(2)
      REAL LABEL
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68     RCSKW1,RCSKW2
      DATA             RCSKW1,RCSKW2 /                                 '
     .$Source: /fs/hseb/ob72/rfc/calb/src/pproc_shared/RCS/bcread.f,v $
     . $',                                                             '
     .$Id: bcread.f,v 1.1 1997/01/23 15:27:15 dws Exp $
     . $' /
C    ===================================================================
C
      DATA BLABEL/4H@B  /,CLABEL/4H@C  /
      DATA DLABEL/4H@D  /,BLNK/4H    /
      DATA THIE/4HTHIE/,CONT/4HCONT/,SMRY/4HSMRY/
C
      NCHAR=2
      LABEL=BLABEL
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,3,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,NBASIN,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,0,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      NCHAR=1
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY1,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY1,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,MULT,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,1,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY2,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY2,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,IT,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,0,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY4,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY4,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY6,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY6,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,IMOW,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,0,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,IMOS,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,0,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 10
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY10,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY10,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
10    CONTINUE
C
      IF(RKEY1.EQ.CLABEL.OR.RKEY1.EQ.BLNK) RKEY1=THIE
      IF(RKEY2.EQ.CLABEL.OR.RKEY2.EQ.BLNK) RKEY2=CONT
      IF(MULT.NE.1.AND.MULT.NE.0) MULT=0
      IF(RKEY4.EQ.CLABEL) RKEY4=BLNK
      IF(RKEY6.EQ.CLABEL) RKEY6=BLNK
      IF(RKEY10.EQ.CLABEL) RKEY10=BLNK
C
      LABEL=CLABEL
20    CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,3,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.7) GO TO 20
C     READ(IN,729)RKEY7,RKEY8,RKEY9,STMNWT
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY7,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY7,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 30
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY8,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY8,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 30
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,UREAL,
     * NCHAR,RKEY9,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,RKEY9,LABEL,2,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
      IF (ISTAT.EQ.6) GO TO 30
      CALL UFIELD(NFLD,ISTRT,LENGTH,ITYPEU,NREP,INTEGR,STMNWT,
     * NCHAR,UCHAR,LLPAR,LRPAR,LASK,LATSGN,LAMPS,LEQUAL,ISTAT)
      CALL AFTERU(LATSGN,ITYPEU,UCHAR,LABEL,1,ISTAT,LENGTH,IERR)
      IF (ISTAT.EQ.6) ISTRT=-1
30    CONTINUE
C
      IF(RKEY7.EQ.DLABEL) RKEY7=BLNK
      IF(RKEY8.EQ.DLABEL.OR.RKEY8.EQ.BLNK) RKEY8=SMRY
      IF(RKEY9.EQ.DLABEL) RKEY9=BLNK
      RETURN
      END
