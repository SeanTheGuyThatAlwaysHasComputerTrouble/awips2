C MEMBER RWBLOCKD
C-----------------------------------------------------------------------
C
C                             LAST UPDATE: 11/23/93.09:27:34 BY $WC20SV
C
C @PROCESS LVL(77)
C
C
C  INITIALIZATION ROUTINE FOR COMMON BLOCKS USED BY NWSRFS CALIBRATION
C  SYSTEM READ/WRITE ROUTINES
C
C
      BLOCK DATA RWBLKD
C
C
C  COMMON BLOCK  /CCFDIM/  DEFINES THE NUMBER AND SIZE OF
C  THE DATA FILES.
C
C     N1    - THE DATA SET REFERENCE NUMBER OF THE FIRST
C             DATA FILE
C     N2    - THE DATA SET REFERENCE NUMBER OF THE LAST
C             DATA FILE
C     NF1   - THE DATA SET REFERENCE NUMBER OF THE FIRST
C             DATA FILE WITH NR1 RECORDS
C     NF2   - THE DATA SET REFERENCE NUMBER OF THE FIRST
C             DATA FILE WITH NR2 RECORDS
C     NF3   - THE DATA SET REFERENCE NUMBER OF THE FIRST
C             DATA FILE WITH NR3 RECORDS
C     NR1   - THE ACTUAL NUMBER OF RECORDS IN THE DATA FILES
C             N1  TO (NF2-1)
C     NR2   - THE ACTUAL NUMBER OF RECORDS IN THE DATA FILES
C             NF2 TO (NF3-1)
C     NR3   - THE ACTUAL NUMBER OF RECORDS IN THE DATA FILES
C             NF3 TO N2
C     NINDX - THE DATA SET REFERENCE NUMBER OF THE INDEX FILE
C     NDR1  - THE DEFAULT NUMBER OF RECORDS IN THE DATA FILES
C             N1  TO (NF2-1)
C     NDR2  - THE DEFAULT NUMBER OF RECORDS IN THE DATA FILES
C             NF2 TO (NF3-1)
C     NDR3  - THE DEFAULT NUMBER OF RECORDS IN THE DATA FILES
C             NF3 TO N2
C
      INCLUDE 'clbcommon/ccfdim'
C
C
C  COMMON BLOCK  /CHFDIM/  DEFINES THE NUMBER AND SIZE OF
C  THE ESP DATA FILES(USED IN ADDITION TO THE DATA FILES
C  DEFINED BY COMMON /CCFDIM/)
C
C     NOTE- 'RECORDS' ARE COUNTED HERE AS 31 WORD RECORDS
C           I.E. ONE RECORD = 31 WORDS = 124 BYTES = 1 MONTH OF
C           DAILY VALUES
C
C
C     IUSESP - INDICATOR TO USE ESP DATA FILES
C               0 = USE CALIBRATION FILES ONLY
C               1 = USE ESP FILES
C     NE1    - DATA SET REFERENCE NUMBER OF FIRST ESP DATA FILE
C     NE2    - DATA SET REFERENCE NUMBER OF LAST ESP DATA FILE
C     NFE1   - DATA SET REFERENCE NUMBER OF FIRST DATA FILE WITH
C              NRE1 RECORDS
C     NFE2   - DATA SET REFERENCE NUMBER OF FIRST DATA FILE WITH
C              NRE2 RECORDS
C     NFE3   - DATA SET REFERENCE NUMBER OF FIRST DATA FILE WITH
C              NRE3 RECORDS
C     NRE1   - NUMBER OF RECORDS IN FILES NE1 TO (NFE2-1)
C     NRE2   - NUMBER OF RECORDS IN FILES NFE2 TO (NFE3-1)
C     NRE3   - NUMBER OF RECORDS IN FILES NFE3 TO NE2
C
      INCLUDE 'clbcommon/chfdim'
C
C    ================================= RCS keyword statements ==========
      CHARACTER*68                RCSKW1,RCSKW2
      COMMON / RCSRWBLOCKD      / RCSKW1,RCSKW2
      DATA                        RCSKW1,RCSKW2 /                      '
     .$Source: /fs/hseb/ob72/rfc/calb/src/block/RCS/rwblockd.f,v $
     . $',                                                             '
     .$Id: rwblockd.f,v 1.1 1995/09/17 19:50:12 dws Exp $
     . $' /
C    ===================================================================
C
C
C
      DATA N1/21/,N2/35/,NINDX/36/
      DATA NR1/7600/,NR2/13300/,NR3/20900/
      DATA NDR1/7600/,NDR2/13300/,NDR3/20900/
      DATA NF1/21/,NF2/25/,NF3/33/
C
      DATA IUSESP/1/
      DATA NE1/37/,NE2/46/
      DATA NFE1/37/,NFE2/40/,NFE3/45/
      DATA NRE1/100000/,NRE2/100000/,NRE3/100000/
      DATA NDRE1/100000/,NDRE2/100000/,NDRE3/100000/
C
C
      END
