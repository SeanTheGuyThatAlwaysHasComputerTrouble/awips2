C
C  COMMON BLOCKS  /CRWBUF/  AND  /CWRBLK/  DEFINE THE NUMBER OF
C  INTERNAL DATA BUFFERS AND THE RECORD PACKING FACTOR.
C
C     NBUF   = THE NUMBER OF BUFFERS
C     MPACK  = MAXIMUM RECORD PACKING FACTOR ALLOWED
C     NPACK  = THE RECORD PACKING FACTOR FOR EACH FILE
C     NWORDS = NUMBER OF WORDS IN EACH TIME SERIES RECORD
C
C     IF NBUF>3 CHANGE THE FOLLOWING VARIABLES
C        BUF(MPACK*NWORDS,NBUF)
C        IRWFLG(NBUF)
C        IPRCNO(NBUF)
C        LREC1(NBUF)
C        LREC2(NBUF)
C        NBUFUN(NBUF)
C
C     IF MPACK*NWORDS>4000 CHANGE THE FOLLOWING VARIABLES
C        BUF(MPACK*NWORDS,NBUF)
C
      INCLUDE 'clbcommon/crwbuf'
      INCLUDE 'clbcommon/crwblk'
C
C
C  COMMON BLOCK  /CIOBUF/  CONTAINS AN ARRAY USED FOR DAIO
C  INPUT/OUTPUT
C
      INCLUDE 'clbcommon/ciobuf'
C
C
C
C  COMMON BLOCK  /CRWCTL/  CONTROLS THE READ-WRITE SUBROUTINE
C  ERROR AND PRINT OPTIONS.
C
C     ISTOP  - OPTION TO HAVE PROGRAM STOP IF ERRORS ENCOUNTERED
C              0 = CONTINUE EXECUTION (IF ERROR ENCOUNTERED, IERROR=1)
C              1 = STOP
C     IERROR - ERROR INDICATOR
C              0 = NO ERROR ENCOUNTERED
C              1 = ERROR ENCOUNTERED
C     IPRINT - OPTION TO PRINT TIME SERIES CREATION MESSAGE
C              0 = MESSAGE NOT PRINTED
C              1 = MESSAGE PRINTED
C     ITSH   - METHOD OF READING TIME SERIES HEADER CARD
C              1 = SHORT FORMAT
C              2 = LONG FORMAT
C     NTSH   - NUMBER OF TIME SERIES IN BLOCK (NOT USED OF ITSH=2)
C     ISFN   - METHOD OF SEARCHING FOR FILE NAMES
C              0 = SEARCH FILE INDEX
C              1 = SEARCH FILE HEADERS
C     IRTSH  - OPTION FOR READING DATA
C              0 = READ MONTHS SEQUENTIALLY
C              1 = READ MONTHS RANDOMLY
C
C
      INCLUDE 'clbcommon/crwctl'
C
C
C
C  COMMON BLOCK  /CRWDEF/  STORES VARIABLES USED BY THE READ/WRITE
C  ROUTINES.
C
C     INTLRD - INDICATOR IF A TIME SERIES HAS BEEN READ
C     INTLWT - INDICATOR IF A TIME SERIES HAS BEEN WRITTEN
C     MAXRD  - MAX NUMBER OF TIME SERIES THAT CAN BE READ AT ONE TIME
C     MAXWT  - MAX NUMBER OF TIME SERIES THAT CAN BE WRITTEN AT ONE TIME
C     MXPB   - MAX NUMBER OF TIME SERIES PER BLOCK
C
      INCLUDE 'clbcommon/crwdef'
C
C  COMMON BLOCK  /CPRCES/  ALLOWS MORE THAN ONE TIME SERIES WITH THE
C  SAME DATA TYPE AND TIME INTERVAL TO BE WRITTEN AT THE SAME TIME
C
      INCLUDE 'clbcommon/cprces'
C
      INCLUDE 'clbcommon/ciox'
C
C  COMMON BLOCK  /CFILEU/  INDICATES IF A FILE HAS BEEN USED.
C
C     IACCES - INDICATOR WHETHER ANY FILE HAS BEEN ACCESSED
C     IFUSED - USAGE INDICATOR FOR REGULAR CALIBRATION FILES
C     IFUSE2 - USAGE INDICATOR FOR HISTORICAL (ESP) CALIBRATION FILES
C     IDEFIN - INDICATOR WHETHER DEFINE FILE ROUTINE HAS BEEN CALLED
C
      INCLUDE 'clbcommon/cfileu'
C
      INCLUDE 'clbcommon/cdtype'
C
      INCLUDE 'clbcommon/cdunit'
C
      INCLUDE 'clbcommon/cbnds'
